    private void TebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TebakActionPerformed
        // TODO add your handling code here:
        
        // jika input angka kosong
        if (InputAngka.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Masukkan Angka Terlebih Dahulu", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
        return;
        }
        
        // Proses tebakan jika masih ada sisa percobaan
        if (percobaan > 0) {
            try {
                int tebak = Integer.parseInt(InputAngka.getText());
                
                // jika angka masih sama dengan angka sebelumnya
                if (tebak == Tebakanterakhir) {
                JOptionPane.showMessageDialog(null, "Ganti Angka Sebelum Mencoba Lagi!", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
                return; 
                }
                
                Tebakanterakhir = tebak;
                
        if (tebak == target) {
            CekAngka.setText("Selamat! Tebakan Anda Benar!!");
             int score;
                if (percobaan == 10) { 
                    score = 100; 
                } else {
                    score = percobaan * 10; 
                }
                
                String skorText = "Skor Anda: " + score;
                Score.setText(String.valueOf(score));


                String name = JOptionPane.showInputDialog("Masukkan Nama Anda:");
                if (name != null && !name.isEmpty()) {
                    scoreListModel.addElement(name + ": " + score);
                    Score.setText(String.valueOf(score));
                    RiwayatScore.append(name + " Score Anda : " + score + "\n");
                }
                
                ResetButton.setText("RESET");
                Tebak.setEnabled(false);
                
                } else {
                    percobaan--; 
                     PerbaruiTebakan();

                    int score = percobaan * 10; 
                    Score.setText(String.valueOf(score));

                    if (tebak < target) {
                        CekAngka.setText("Tebakan terlalu kecil!");
                    } else if (tebak > target) {
                        CekAngka.setText("Tebakan terlalu besar!");
                    }
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Masukkan Angka Yang Valid", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
            }
        }
        
        // Kondisi jika percobaan habis dan gagal
        if (percobaan == 0 && Tebak.isEnabled()) {
        CekAngka.setText("Anda Gagal! Target: " + target);
        ResetButton.setText("RESET");
        Tebak.setEnabled(false);
        }
    }//GEN-LAST:event_TebakActionPerformed

    private void ResetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ResetButtonActionPerformed
        // TODO add your handling code here:
        if (ResetButton.getText().equals("START") || ResetButton.getText().equals("RESET")) {
        resetGame();
        ResetButton.setText("RISET");
        } else if (ResetButton.getText().equals("RISET")) {
            Score.setText("");
            resetGame();
        }
    }//GEN-LAST:event_ResetButtonActionPerformed

