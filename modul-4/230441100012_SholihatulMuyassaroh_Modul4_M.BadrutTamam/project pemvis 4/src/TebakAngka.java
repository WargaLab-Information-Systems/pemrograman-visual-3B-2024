    private void startGame(JButton btreset, JTextField tftebakan, JTextField tfpetunjuk) {
        resetGame();
        btreset.setEnabled(false);
        tftebakan.setText("");
        tfpetunjuk.setText("");
        tfscore.setText("100"); 
        btreset.setText("Reset"); 
        btreset.setEnabled(true);
    }

    private void resetGame() {
        Random rand = new Random();
        angkaTebakan = rand.nextInt(100) + 1;
        kesempatan = 10;
        Score = 100; 
        tfscore.setText("100"); 
        tfpetunjuk.setText(""); 
        tftebakan.setText("");
    }
    
