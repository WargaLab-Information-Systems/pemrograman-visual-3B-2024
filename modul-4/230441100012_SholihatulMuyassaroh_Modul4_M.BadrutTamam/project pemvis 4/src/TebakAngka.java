    private void btresetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btresetActionPerformed
        // TODO add your handling code here:
        if (btreset.getText().equals("Start")) {
        startGame(btreset, tftebakan, tfpetunjuk);
        JOptionPane.showMessageDialog(null, "Permainan Dimulai! \n Silahkan tebak angkanya");
        tftebakan.requestFocus();
        } else {
            int confirm = JOptionPane.showConfirmDialog(null, "Anda yakin mengakhiri permainan sebelumnya?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                resetGame();
                tftebakan.setText("");
                tfpetunjuk.setText("");
                tfscore.setText("100"); 
                btreset.setText("Start"); 
            }
        }
    }//GEN-LAST:event_btresetActionPerformed

    private void bttebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttebakActionPerformed
        // TODO add your handling code here:
        if (btreset.getText().equals("Start")) {
            JOptionPane.showMessageDialog(null,"Tekan Start untuk memulai permainan!", "WARNING", JOptionPane.WARNING_MESSAGE);
            tftebakan.setText(""); 
            tftebakan.requestFocus();
            return;
        }

        String input = tftebakan.getText();
        boolean validInput = true;
        for (int i = 0; i < input.length(); i++) {
            if (!Character.isDigit(input.charAt(i))) {
                validInput = false;
                break;
            }
        }

        if (validInput && !input.isEmpty()) {
            int tebakanUser   = Integer.parseInt(input);
            if (tebakanUser   < 1 || tebakanUser   > 100) {
                JOptionPane.showMessageDialog(null, "Masukkan angka antara 1 dan 100!","WARNING", JOptionPane.ERROR_MESSAGE);
                tftebakan.requestFocus();
            } else {
                if (tebakanUser   < angkaTebakan) {
                    tfpetunjuk.setText("Terlalu kecil!");
                    kesempatan--;
                } else if (tebakanUser   > angkaTebakan) {
                    tfpetunjuk.setText("Terlalu besar!");
                    kesempatan--;
                } else if (tebakanUser   == angkaTebakan) {
                    Score = kesempatan * 10;
                    tfscore.setText(String.valueOf(Score));
                    tfpetunjuk.setText("Saya adalah angka " + angkaTebakan );
                    JOptionPane.showMessageDialog(null, "Tebakan Anda Benar" + ". Selamat!");
                    String nama = JOptionPane.showInputDialog("Masukkan Nama Anda:");
                    if (nama != null && !nama.isEmpty()) {
                        riwayatSkor.add(nama + " - Score: " + Score);
                        jList1.setListData(riwayatSkor.toArray(new String[0])); // Upd list
                    }else {
                        riwayatSkor.add("user" + " - Score: " + Score);
                        jList1.setListData(riwayatSkor.toArray(new String[0]));
                    }
                    jTabbedPane1.setSelectedIndex(1);
                    // Reset, kembali ke Start
                    resetGame();
                    btreset.setText("Start"); 
                    return; 
                }

                // Update skor (salah)
                if (kesempatan < 10) {
                    Score -= 10; // Kurangi 10 
                    tfscore.setText(String.valueOf(Score)); // Upd skor
                }

                //  tampilkan sisa tebakan 
                if (kesempatan > 0) {
                    JOptionPane.showMessageDialog(null, "Sisa tebakan anda tersisa " + kesempatan + " kali.");
                }

                tftebakan.setText(""); 
                tftebakan.requestFocus(); 

                if (kesempatan == 0) {
                    JOptionPane.showMessageDialog(null, "Anda gagal. Tetap semangat dan jangan menyerah!");
                    resetGame();
                    tftebakan.setText("");
                    tfpetunjuk.setText("");
                    tfscore.setText("0");
                    btreset.setText("Start"); // Ubah teks(permainan selesai)
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Masukkan angka yang valid!","PERINGATAN",JOptionPane.ERROR_MESSAGE);
            tftebakan.setText("");
            tftebakan.requestFocus(); 
        }
    }//GEN-LAST:event_bttebakActionPerformed

