    private void resetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetActionPerformed
                                     
     // Mengatur ulang permainan
    reset.setText("Reset");
    bilRandom = rd.nextInt(100) + 1; // Range 1-100
    tebak.setText("");
    cek.setText("");
    score.setText("100"); // Set skor awal menjadi 100
    attempts = 10; // Mengatur kembali attempts ke 10
    

    // Aktifkan kembali input dan tombol yang dinonaktifkan
    tebak.setEnabled(true);
    yote.setEnabled(true);
    cek.setEnabled(true);
    score.setEnabled(true);

    tebak.requestFocus(); // Fokus kembali ke input tebakan


    }//GEN-LAST:event_resetActionPerformed

