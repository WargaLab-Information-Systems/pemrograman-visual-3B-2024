    private void yoteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yoteActionPerformed
                                                                           
    try {
        // Atur skor awal jika belum di-set
        int skor = score.getText().isEmpty() ? 100 : Integer.parseInt(score.getText());

        // Periksa apakah input tebakan kosong
        if (tebak.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukkan angka tebakan terlebih dahulu!");
            return; // Keluar dari metode jika input kosong
        }

        // Coba mengonversi input ke integer
        int x = Integer.parseInt(tebak.getText()); // Input tebakan pengguna

        if (x == bilRandom) { // Cek apakah tebakan benar
            cek.setText("Benar!");

            // Tampilkan dialog untuk input nama
            String nama = JOptionPane.showInputDialog(null, "Tebakan Anda benar! Masukkan nama Anda:");

            // Jika nama kosong atau pengguna menekan Cancel, set nama menjadi "Pemain"
            if (nama == null || nama.isEmpty()) {
                nama = "Pemain";
            }

            // Tambahkan nama dan skor ke dalam JTextArea hasil
            hasil.append(nama + " - " + skor + "\n");

            // Reset permainan
            JOptionPane.showMessageDialog(null, "Benar Sekali!");
            reset.setText("Start");
            bilRandom = rd.nextInt(100) + 1; // Generate angka random baru antara 1-100
            attempts = 10; // Reset percobaan jika benar
            jTabbedPane1.setSelectedIndex(1);

            // Nonaktifkan input dan tombol agar permainan tidak dapat dilanjutkan
            tebak.setEnabled(false);
            yote.setEnabled(false);

        } else {
            attempts--; // Kurangi percobaan menjadi 9 setelah satu kesalahan
            skor -= 10; // Kurangi skor sebesar 10 jika tebakan salah

            if (attempts > 0) {
                if (x > bilRandom) {
                    cek.setText("Terlalu Besar!");
                    JOptionPane.showMessageDialog(null, "Tebakan terlalu besar! Anda memiliki " + attempts + "x percobaan lagi.");
                    tebak.setText("");
                    tebak.requestFocus();
                } else {
                    cek.setText("Terlalu Kecil!");
                    JOptionPane.showMessageDialog(null, "Tebakan terlalu kecil! Anda memiliki " + attempts + "x percobaan lagi.");
                    tebak.setText("");
                    tebak.requestFocus();
                }
            } else {
                skor = 0; // Set skor ke 0 jika percobaan habis
                JOptionPane.showMessageDialog(null, "Game Over! Anda telah mencapai batas percobaan. Tekan tombol 'Reset' untuk memulai kembali.");
                reset.setText("Start");
                
                // Nonaktifkan input dan tombol terkait
                tebak.setEnabled(false);
                yote.setEnabled(false);
                cek.setEnabled(false);
            }
        }

        // Set skor baru
        score.setText(String.valueOf(skor));

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Masukkan angka yang valid!");
    
} 

    }//GEN-LAST:event_yoteActionPerformed

