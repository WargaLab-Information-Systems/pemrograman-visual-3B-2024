    private void TebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TebakActionPerformed
        // TODO add your handling code here:
    // Proses tebakan jika masih ada sisa percobaan
    if (attempts > 0) {
        try {
            int guess = Integer.parseInt(Main1.getText()); // Ambil input dari pengguna
            if (guess == target) {
                Main2.setText("Selamat! Tebakan Anda Benar!!");
                Main4.setText(String.valueOf(target));

                // Skor 100 jika langsung benar pada tebakan pertama
                int score;
                if (attempts == 10) { 
                    score = 100; // Skor tetap 100 jika benar pada tebakan pertama
                } else {
                    score = attempts * 10; // Skor dihitung berdasarkan tebakan yang tersisa
                }
                
                String skorText = "Skor Anda: " + score;
                Main3.setText(String.valueOf(score));
                Tempat.append(skorText + "\n");

                String name = JOptionPane.showInputDialog("Masukkan Nama Anda:");
                if (name != null && !name.isEmpty()) {
                    scoreListModel.addElement(name + ": " + score);
                    Main3.setText(String.valueOf(score));
                    Tempat.append(name + ": " + score + "\n");
                }
                
                Mulai.setText("RESET");
                Tebak.setEnabled(false); // Menonaktifkan tombol setelah menang
            } else {
                attempts--; // Mengurangi percobaan jika tebakan salah
                PerbaruiTebakan();

                // Memberi feedback apakah tebakan terlalu kecil atau besar
                int score = attempts * 10;
                Main3.setText(String.valueOf(score));
                if (guess < target) {
                    Main2.setText("Angka terlalu kecil!");
                } else if (guess > target) {
                    Main2.setText("Angka terlalu besar!");
                }
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Masukkan Angka Yang Valid", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    // Kondisi jika percobaan habis dan pengguna gagal
    if (attempts == 0 && Tebak.isEnabled()) {
        Main2.setText("Anda Gagal Dalam Permainan!");
        Mulai.setText("RESET");
        Tebak.setEnabled(false);
    }

    }//GEN-LAST:event_TebakActionPerformed

