    private void resetGame() {
        gameStarted = false;
        percobaan = 10;
        tfTebak.setText("");
        tfCek.setText("");
        tfScore.setText("");
        btnTebak.setEnabled(false);
        ubah.setText("Start");
    }
    
    private void startGame() {
        gameStarted = true;
        percobaan = 10;
        score = 0;
        angkaTarget = new Random().nextInt(100) + 1;
        System.out.println(angkaTarget);
        tfCek.setText("");
        tfScore.setText("");
        btnTebak.setEnabled(true);
        ubah.setText("Reset");
    }
