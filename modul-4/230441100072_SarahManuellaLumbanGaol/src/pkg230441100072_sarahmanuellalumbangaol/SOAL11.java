    private void updateScoreHistory() {
        String data = nama + " - Score: " + score;
        scoreHistory.add(data);
        StringBuilder sb = new StringBuilder();
        for (String scoreEntry : scoreHistory) {
            sb.append(scoreEntry).append("\n");
        }
        historyArea.setText(sb.toString());
    }

