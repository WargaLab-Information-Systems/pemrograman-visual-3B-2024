    private void tebakangka() {
        try {
            int tebak = Integer.parseInt(input.getText());
            kesempatan++;
            int sisaa = maxkesempatan - kesempatan;
            sisa.setText(String.valueOf(sisaa));
            cek.setText("Tebak " + "(" + sisaa + ")");
            scoreField.setText("");
            score = String.valueOf((maxkesempatan - kesempatan) * 10);
            if (tebak == 0 || tebak > 100) {
                JOptionPane.showMessageDialog(null, "Aku hanya angka dari 1-100", "Warning!", JOptionPane.WARNING_MESSAGE);
                input.setText("");
                input.requestFocus();
            } else if (tebak < angkaacak) {
                resultField.setText("Aku Adalah Angka yang lebih besar");
                scoreField.setText(score);
                input.setText("");
                input.requestFocus();
            } else if (tebak > angkaacak) {
                resultField.setText("Aku Adalah Angka yang lebih kecil");
                scoreField.setText(score);
                input.setText("");
                input.requestFocus();

            } else {
                resultField.setText("aku adalah angka " + angkaacak);
                JOptionPane.showMessageDialog(this, "U DID IT BROO!!", "INFO", JOptionPane.INFORMATION_MESSAGE);
                score = String.valueOf((maxkesempatan - kesempatan) * 10 + 10);
                scoreField.setText((score));
                nama = JOptionPane.showInputDialog(this, "Masukkan Nama Anda :");
                if (nama == null || nama.trim().isEmpty()) {
                    nama = "user undifined 😀";
                }
                updateScoreHistory();
                jTabbedPane2.setSelectedIndex(1);
                mulaiGame();
                return;
            }
            if (sisaa == 0) {
                JOptionPane.showMessageDialog(this, "Anda Gagal! Kesempatan habis.Coba Lagi pls");
                score = String.valueOf((maxkesempatan - kesempatan) * 10 + 10);
                scoreField.setText(score);
                mulaiGame();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Masukkan Angka Yang Valid", "warning!", JOptionPane.WARNING_MESSAGE);
            input.setText("");
            input.requestFocus();
        }
    }//GEN-LAST:event_cekActionPerformed

