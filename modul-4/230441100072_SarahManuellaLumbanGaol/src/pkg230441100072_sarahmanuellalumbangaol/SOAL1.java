    private void checkGuess() {
        try {
            int guess = Integer.parseInt(input.getText());
            attempts=0;
            attempts++;

            if (guess < targetNumber) {
                resultField.setText("Aku Adalah Angka " + targetNumber );
            } else if (guess > targetNumber) {
                resultField.setText("Aku Adalah Angka " + targetNumber );
            } else {
                // Tebakan benar
                resultField.setText("Anda Berhasil!");
                
                // Hitung skor
                int score = (maxAttempts - attempts) * 10;
                scoreField.setText("Score: " + score);
                
                // Input nama pengguna
                String name = JOptionPane.showInputDialog(this, "Masukkan Nama Anda:");
                if (name != null && !name.trim().isEmpty()) {
//                    scoreHistory.add(name + " - Score: " + score);
                    updateScoreHistory();
                }
                
                resetGame();
                return;
            }

            // Periksa apakah jumlah tebakan sudah mencapai batas
            if (attempts >= maxAttempts) {
                JOptionPane.showMessageDialog(this, "Anda Gagal! Kesempatan habis.");
                resetGame();
            }

        } catch (NumberFormatException ex) {
            resultField.setText("Masukkan angka yang valid!");
        }
    
        
    }//GEN-LAST:event_cekActionPerformed

