    private void TstartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TstartActionPerformed
        if (statusGame) {
            Tstart.setText("Reset");
            tebakan = 10;
            Ttebak.setText("tebak(" + tebakan + ")");
            tfCek.setText("");
            tfCobaTebak.setText("");
        } else {
            statusGame = true;
            Ttebak.setEnabled(true);
            tfCobaTebak.setEditable(true);
            Tstart.setText("Reset");
        }
        
        Random random = new Random();
        angka = random.nextInt(100); 
        
        
    }//GEN-LAST:event_TstartActionPerformed
    
    public  void reset() {
        statusGame = false;
        Ttebak.setEnabled(false);
        tfCobaTebak.setEditable(false);
        Tstart.setText("Start");
        tfCek.setText("");
        tfCobaTebak.setText("");
        tfScore.setText("");
        tebakan = 10;
        Ttebak.setText("tebak(" + tebakan + ")");
    }
    
    private void TtebakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TtebakActionPerformed
        try {
            if (tebakan <= 0) {
                JOptionPane.showMessageDialog(null, "ASTAGFIRULLAH", "ANDA GAGAL", JOptionPane.INFORMATION_MESSAGE);
                Tstart.setEnabled(false);
                Ttebak.setEnabled(false);
                tfCobaTebak.setEditable(false);
                tfCek.setText("");
                tfCobaTebak.setText("");
                tfScore.setText("");
                
            } else {
                if (tfCobaTebak.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Anda Belum Menebak, Silakan isi Form Coba Tebak", "Warning", JOptionPane.WARNING_MESSAGE);
                }else {
                    int cobaTebak = Integer.parseInt(tfCobaTebak.getText());
                    if (cobaTebak == angka) {
                        tfCek.setText("Aku adalah angka " + angka);
                        int HasilScore = tebakan * 10; 
                        tfScore.setText(Integer.toString(HasilScore));
                        String nama = JOptionPane.showInputDialog(null, "Masukkan Nama:", "Anda Berhasil", JOptionPane.INFORMATION_MESSAGE);
                        
                        jTabbedPane1.setSelectedIndex(1);
                        scoreHistory.add(nama + " : " + HasilScore);
                        taScore.setText("");
                        for (String data : scoreHistory) {
                            taScore.append(data + "\n"); 
                        }
                        reset();
                    }
                    if (cobaTebak > angka) {
                        tfCek.setText("Terlalu Kebesaran");
                        tebakan--;
                        Ttebak.setText("tebak(" + tebakan + ")");
                    }
                    if (cobaTebak < angka) {
                        tfCek.setText("Terlalu Kekecilan");
                        tebakan--;
                        Ttebak.setText("tebak(" + tebakan + ")");
                    }
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ANGKA WOI BUKAN HURUF", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_TtebakActionPerformed

    
