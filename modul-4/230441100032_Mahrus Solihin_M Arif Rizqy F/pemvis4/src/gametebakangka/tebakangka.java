    private void startActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startActionPerformed
        try {
        if (start.getText().equals("Start")) {
            target = (int) (Math.random() * 100) + 1;
            kesempatan = 10;
            start.setText("Tebak (10)");
            tebakan.setText("");
            score = 0;
            scor.setText(String.valueOf(score));
            return;
        }

        int tebak = Integer.parseInt(tebakan.getText());

        if (tebak < 1 || tebak > 100) {
            JOptionPane.showMessageDialog(this, "Masukkan angka antara 1-100");
            return;
        }

        kesempatan--;
        start.setText("Tebak (" + kesempatan + ")");

        if (tebak < target) {
            cek.setText("Tebakan terlalu kecil!");
        } else if (tebak > target) {
            cek.setText("Tebakan terlalu besar!");
        } else {
            // Jika tebakan benar, hitung skor dan tampilkan pesan berhasil
            score = (kesempatan + 1) * 10;
            scor.setText(String.valueOf(score));
            cek.setText("Selamat! Anda menebak angka " + tebak + " dengan benar.");

            String name;
            do {
                name = JOptionPane.showInputDialog("Masukkan nama Anda:");
                    if (name == null || name.trim().isEmpty()) {
                        JOptionPane.showMessageDialog(null, "Nama tidak boleh kosong. Silakan masukkan nama!");
                    }
            } while (name == null || name.trim().isEmpty());

            
            listscore.add(name + " Mendapatkan Skor " + score); 
            listscore.repaint();
            JOptionPane.showMessageDialog(this, "Selamat " + name + "! Skor Anda: " + score);

            resetGame();
            start.setText("Start");
            return;
        }

        if (kesempatan == 0) {
            JOptionPane.showMessageDialog(this, "Kesempatan habis! Angka yang benar adalah " + target);
            resetGame();
            start.setText("Start");  // Kembalikan tombol ke "Start"
        }

        tebakan.setText("");
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan angka yang valid");
    }
    }//GEN-LAST:event_startActionPerformed

