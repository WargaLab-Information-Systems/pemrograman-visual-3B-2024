    private void btnokeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnokeActionPerformed
        String kesempatan = inkesempatan.getText();
        int intkesempatan = Integer.parseInt(kesempatan);
        String jawaban = outjawaban.getText();
        int intjawaban = Integer.parseInt(jawaban);
        inkesempatan.setVisible(false);
        
        if (count > 0){
            inkesempatan.setVisible(true);
            if (intkesempatan < intjawaban){
                labelkesempatan.setText("Tebakan anda terlalu kecil");         
            }else if (intkesempatan > intjawaban){
                labelkesempatan.setText("Tebakan anda terlalu besar");    
            }else if (intkesempatan == intjawaban){
                labelkesempatan.setText("jawaban anda benar, silahkan cek score anda");
                nilai = 10 * count;
                btnoke.setEnabled(false);
                String strnilai = Integer.toString(nilai);
                urutan = urutan + (user + " berhasil menebak pada kesempatan " + (10 - count) + " dengan score " + strnilai + "\n");
                history.setText(urutan);
            }
            JOptionPane.showMessageDialog(null, "Tebakan anda masuk", "Pengecekan", JOptionPane.INFORMATION_MESSAGE);
        }else if(count == 0){
            labelkesempatan.setText("kesempatan habis");
            btnoke.setEnabled(false);
            nilai = 10 * count;
            String strnilai = Integer.toString(nilai);
            urutan = urutan + (user + " gagal menebak angka " + ", score akhir " + strnilai + "\n");
            history.setText(urutan);
            JOptionPane.showMessageDialog(null, "Silahkan Main lagi", "Game Over", JOptionPane.ERROR_MESSAGE);
        }
        
        count = count - 1;
    }//GEN-LAST:event_btnokeActionPerformed

