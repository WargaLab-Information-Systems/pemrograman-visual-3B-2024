    private void ButtonProyek1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonProyek1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = TabelProyek.getSelectedRow();
        if (selectedRow == -1) {
            System.out.println("Pilih baris yang akan diubah");
            return;
        }

        // Mendapatkan nilai ID dari tabel dan memeriksa apakah nilainya tidak kosong
        Object idValue = model2.getValueAt(selectedRow, 0);

        if (idValue == null || idValue.toString().isEmpty()) {
            System.out.println("ID tidak valid atau kosong.");
            return;
        }

        try {
            int id = Integer.parseInt(idValue.toString());

            // Mendapatkan nilai dari JTextField untuk durasi dan nama proyek
            int durasi = Integer.parseInt(InputProyek3.getText());  // Pastikan Durasi diambil dari JTextField
            String nama_proyek = InputProyek2.getText();

            // Membuat objek BarangProyek dengan data yang diperbarui
            BagianProyek barang = new BagianProyek(id, durasi, nama_proyek);

            // Mengupdate data proyek di database
            try {
                Proyek.updateProyek(barang);
                loadDataProyek();
                System.out.println("Data berhasil diubah");
            } catch (SQLException e) {
                System.out.println("Tidak dapat mengubah data, error: " + e.getMessage());
            }
        } catch (NumberFormatException e) {
            System.out.println("ID atau Durasi tidak valid: " + e.getMessage());
        }
    }//GEN-LAST:event_ButtonProyek1ActionPerformed

