    public void loadDataKaryawan() {
        model1.setRowCount(0);
        Pilihan1.removeAllItems();
        Pilihan1.addItem("-- Karyawan--");
        try {
            List<BagianKaryawan> list_karyawan = Karyawan.getAllKaryawan();
            
            for (BagianKaryawan brg: list_karyawan) {
                model1.addRow(
                    new Object[]{
                       brg.getId(),
                       brg.getNama(),
                       brg.getJabatan(),
                       brg.getDapartemen()
                    }
            );
                
            // Tambah data ke ComboBox (ID Karyawan)
            Pilihan1.addItem(brg.getId() + " - " + brg.getNama());
        }
            
        TabelKaryawan.setModel(model1);
        
        } catch (SQLException e) {
            System.out.println("tidak dapat memuat data, error" + e);
        }
    }
    
    public void loadDataProyek() {
        model2.setRowCount(0);
        Pilihan2.removeAllItems();
        Pilihan2.addItem("-- Proyek --");

        try {
            List<BagianProyek> list_proyek = Proyek.getAllProyek();
            
            for (BagianProyek brg: list_proyek) {
                model2.addRow(
                    new Object[]{
                       brg.getId(),
                       brg.getDurasi(),
                       brg.getNamaProyek()
                    }
            );
            
            Pilihan2.addItem(brg.getId() + " - " + brg.getNamaProyek());
        }
            
        TabelProyek.setModel(model2);
        
        for (ActionListener al : Pilihan2.getActionListeners()) {
            Pilihan2.removeActionListener(al);
        }
        
        Pilihan2.addActionListener(e -> {
            // Ambil ID proyek dari item yang dipilih
            String selectedItem = (String) Pilihan2.getSelectedItem();
            if (selectedItem != null) {
                String idProyek = selectedItem.split(" - ")[0]; // Mendapatkan ID proyek

                // Cari durasi proyek yang sesuai
                for (BagianProyek brg : list_proyek) {
                    if (String.valueOf(brg.getId()).equals(idProyek)) {
                        InputTransaksi1.setText(String.valueOf(brg.getDurasi()));
                        break;
                    }
                }
            }
        });
        } catch (SQLException e) {
            System.out.println("tidak dapat memuat data, error" + e);
        }
    }
    
