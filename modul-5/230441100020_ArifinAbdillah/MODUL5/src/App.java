    public void loadDataKaryawan() {
        model1.setRowCount(0);
        PilihanKaryawan.removeAllItems();
        PilihanKaryawan.addItem("-- Karyawan --");
        try {
            List<BarangKaryawan> list_karyawan = Karyawan.getAllKaryawan();
            
            for (BarangKaryawan brg: list_karyawan) {
                model1.addRow(
                    new Object[]{
                       brg.getId(),
                       brg.getNama(),
                       brg.getJabatan(),
                       brg.getDapartemen()
                    }
            );
                
            PilihanKaryawan.addItem(brg.getId() + " - " + brg.getNama());
        }
            
        TabelKaryawan.setModel(model1);
        
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Tidak dapat memuat data, error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void loadDataProyek() {
    model2.setRowCount(0);
    PilihanProyek.removeAllItems();
    PilihanProyek.addItem("-- Proyek --");


    try {
        List<BarangProyek> list_proyek = Proyek.getAllProyek();

        for (BarangProyek brg : list_proyek) {
            model2.addRow(new Object[]{
                brg.getId(),
                brg.getDurasi(),
                brg.getNamaProyek()
            });

            PilihanProyek.addItem(brg.getId() + " - " + brg.getNamaProyek());
        }

        TabelProyek.setModel(model2);
        
        for (ActionListener al : PilihanProyek.getActionListeners()) {
            PilihanProyek.removeActionListener(al);
        }

        PilihanProyek.addActionListener(e -> {
            // Ambil ID proyek dari item yang dipilih
            String selectedItem = (String) PilihanProyek.getSelectedItem();
            if (selectedItem != null) {
                String idProyek = selectedItem.split(" - ")[0]; 

                // Cari durasi proyek yang sesuai
                for (BarangProyek brg : list_proyek) {
                    if (String.valueOf(brg.getId()).equals(idProyek)) {
                        InputDurasi.setText(String.valueOf(brg.getDurasi()));
                        break;
                    }
                }
            }
        });

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Tidak dapat memuat data, error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
