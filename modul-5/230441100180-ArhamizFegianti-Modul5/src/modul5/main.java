    public void loadDataKaryawan(){
        model.setRowCount(0);
        
        try{
            List<Karyawan> list_karyawan = karyawan1.getAllKaryawan();
            for (Karyawan kry : list_karyawan){
                model.addRow(
                        new Object[]{
                           kry.getId(),
                           kry.getNamaK(),
                           kry.getJab(),
                           kry.getDep()
                        }
                );
            }
            jTableK.setModel(model);

        } catch (SQLException e){
            System.out.println("Tidak dapat memuat data, error" + e);
        }
    }
    
    
