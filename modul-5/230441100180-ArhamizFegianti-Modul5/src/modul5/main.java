    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String peran = jTPeran.getText();
        String namaKaryawan = (String) jComboBox1.getSelectedItem();
        String namaProyek = (String) jComboBoxProyek.getSelectedItem();
        
        if (jTPeran.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Isi Form Terlebih Dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        jTPeran.setText("");
        jTIDKaryawan.requestFocus();
        return;
        }

        // Dapatkan ID karyawan dan proyek dari Map
        int idKaryawan = karyawanMap.getOrDefault(namaKaryawan, -1);
        Proyek proyek = proyekMap.get(namaProyek); // Dapatkan objek Proyek berdasarkan namaProyek
        int idProyek = (proyek != null) ? proyek.getId() : -1; // Jika proyek tidak null, ambil id; jika null, gunakan -1

        // Validasi ID karyawan dan proyek
        if (idKaryawan == -1 || idProyek == -1) {
            System.out.println("Karyawan atau proyek tidak valid");
            return;
        }

        // Buat objek Transaksi dengan ID yang valid
        Transaksi trs = new Transaksi(idKaryawan, idProyek, peran);

        try {
            transaksi1.insertTransaksi(trs);
            loadDataTransaksi();
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
            jTIDKaryawan.setText("");
            jTPeran.setText("");
            return;
            
        } catch (SQLException e) {
            System.out.println("Tidak dapat menambahkan transaksi, error: " + e);
        }

    }//GEN-LAST:event_jButton7ActionPerformed

