    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
    if (!benar.isSelected()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Silakan centang kotak untuk melanjutkan.", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;    
    }    
    String namaInput = nama.getText();
    String alamatInput = alamat.getText();
    String tanggallahirInput = tanggallahir.getText();
    String nohpInput = nohp.getText();
    String gmailInput = gmail.getText();
    String prodiInput = prodi.getText();
    String fakultasInput = (String) fakultas.getSelectedItem(); 
    
    String jeniskelaminInput = "";
    if (lakilaki.isSelected()) {
        jeniskelaminInput = "Laki-laki";
    } else if (perempuan.isSelected()) {
        jeniskelaminInput = "Perempuan";
    }else{
                  javax.swing.JOptionPane.showMessageDialog(this, "pilih salah 1", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
}
    
    
    pemdaftaran2 form2 = new pemdaftaran2();
    form2.setData(namaInput, alamatInput, tanggallahirInput, nohpInput, jeniskelaminInput, gmailInput, prodiInput, fakultasInput);
    form2.setVisible(true);
    
    this.dispose();
    

    }//GEN-LAST:event_simpanActionPerformed

