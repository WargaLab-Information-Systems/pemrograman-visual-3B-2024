    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        if (NamaLengkap.getText().isEmpty()||
                Nim.getText().isEmpty()||
                Ttl.getText().isEmpty()||
                (!LakiLaki.isSelected() && !Perempuan.isSelected())||
                ALamat.getText().isEmpty()||
                TElepon.getText().isEmpty()|| EMail.getText().isEmpty()||
                Prodi.getSelectedIndex()== -1 ||
                !jCheckBox1.isSelected()){
            javax.swing.JOptionPane.showMessageDialog(this, "Lengkapi semua dan konfirmasi.","ERROR", javax.swing.JOptionPane.ERROR_MESSAGE);
            }else{
//            
//            String jComboBox = jComboBox1.getSelectedItem().toString();
            String Nama = NamaLengkap.getText();
            String NIM = Nim.getText();
            String TTL = Ttl.getText();
            String Alamat = ALamat.getText();
            String JenisKelamin = LakiLaki.isSelected()? "Laki-Laki" : "Perempuan";
            String Telepon = TElepon.getText();
            String Email = EMail.getText();
            String ProgramStudi = (String) Prodi.getSelectedItem();
        

            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
            
            //menampilkan ke frame 2
            kedua Kedua = new kedua(Nama, NIM, TTL, Alamat, JenisKelamin, Telepon, Email, ProgramStudi.trim()); 
            Kedua.setVisible(true); //menampilkan di frame 2
            this.dispose(); 
        }
    }//GEN-LAST:event_SimpanActionPerformed

