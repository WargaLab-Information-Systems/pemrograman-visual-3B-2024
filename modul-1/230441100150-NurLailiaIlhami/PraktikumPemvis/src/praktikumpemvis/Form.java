    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if (!cbcentang.isSelected()){
            JOptionPane.showMessageDialog(this, "centangg terlebih dahulu","peringatan", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else{
           if (tfNama.getText().isEmpty() || tfTanggalLahir.getText().isEmpty() || tfAlamat.getText().isEmpty()
            || tfNoTelpon.getText().isEmpty() || tfEmail.getText().isEmpty() || CBJurusan.getSelectedIndex() == -1
            || (!RBlakilaki.isSelected() && !RBperempuan.isSelected())) {
            JOptionPane.showMessageDialog(this, "Tolong isi semua data terlebih dahulu", "Peringatan", JOptionPane.ERROR_MESSAGE);
            return;
        }else{
            Hasil hasil = new Hasil();
            hasil.tfNama.setText(tfNama.getText());
            hasil.tftempatlahir.setText(tfTanggalLahir.getText());
            hasil.tfAlamat.setText(tfAlamat.getText());
            hasil.tfNotelp.setText(tfNoTelpon.getText());
            hasil.Email.setText(tfEmail.getText());
            if(RBlakilaki.isSelected()){
                hasil.tfjeniskelamin.setText("Laki Laki");
            }
            if(RBperempuan.isSelected()){
                hasil.tfjeniskelamin.setText("Perempuan");
            }
            String jlJurusan =  (String)CBJurusan.getSelectedItem().toString();
            hasil.tfJurusan.setText(jlJurusan);
            
            hasil.setVisible(true);
        }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

