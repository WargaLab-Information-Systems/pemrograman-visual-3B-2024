    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
         if (!validate.isSelected()) {
            // Menampilkan pesan peringatan
            JOptionPane.showMessageDialog(this, 
                "Pilih Selesai Dahulu.", 
                "Peringatan", 
                JOptionPane.WARNING_MESSAGE);
        } else {
   // Ambil data dari form
    String namaOrtu = inpwali.getText(); 
    String alamat = inpalamat.getText(); 
    String notelp = inptlp.getText(); 
    String namaAnk = inpananda.getText(); 
    String tglLahir = inpbirth.getText();
    String jenisKelamin = laki.isSelected() ? "Laki-Laki" : "Perempuan"; 
    String minat = (String) bakat.getSelectedItem(); 
    String kebutuhankhusus = ""; 

    // Cek pilihan kelas tambahan
    if (intel.isSelected()) kebutuhankhusus += "Intelektual ";
    if (fisk.isSelected()) kebutuhankhusus += "Fisik ";
    if (sosl.isSelected()) kebutuhankhusus += "Sosial ";
    if (none.isSelected()) kebutuhankhusus += "Tidak Ada ";

    // Buat instance dari Frame2 dan kirim data
    Frame2 frame2 = new Frame2(namaOrtu, alamat, notelp, namaAnk, tglLahir, jenisKelamin, minat,kebutuhankhusus.trim());
    frame2.setVisible(true); 
    this.dispose(); 

       }
    }//GEN-LAST:event_saveActionPerformed

