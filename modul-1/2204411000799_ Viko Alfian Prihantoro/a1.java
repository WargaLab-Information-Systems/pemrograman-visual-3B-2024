    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String N_LENGKAP = jTextField1.getText();
        String ALAMAT = jTextField2.getText();
        String NO_HP = jTextField3.getText();
        String PRODI = jTextField4.getText();
        
        String J_KELAMIN = "";
        if (jRadioButton1.isSelected()){
            J_KELAMIN = ("Laki-Laki");
        }else{
            J_KELAMIN = ("Perempuan");
        }
        
        String FAKULTAS = (String) jComboBox1.getSelectedItem();
        a2 frame2 = new a2(N_LENGKAP, ALAMAT, NO_HP, PRODI, FAKULTAS, J_KELAMIN);
        // Cek apakah checkbox dicentang
    if (!jCheckBox1.isSelected()) {
        // Jika checkbox tidak dicentang, tampilkan pesan
        JOptionPane.showMessageDialog(this, "Checkbox tidak dicentang. Harap centang terlebih dahulu.", "Peringatan", JOptionPane.WARNING_MESSAGE);
    } else {
        // Jika checkbox dicentang, pindah ke panel berikutnya (panel kedua)
        this.dispose(); // Menutup panel saat ini
        
        // Membuka panel kedua (misalnya jendela a2)
        frame2.setVisible(true);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

