    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
       nama = nm.getText(); 
       tempat = tmpt.getText();
       tanggal = tgl.getText();
       
       if (perempuan.isSelected() == true){
            jeniskelamin = "Perempuan";
        } else if (laki.isSelected() == true){
            jeniskelamin = "Laki-Laki";
        } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Jenis kelamin harus diisi!");
        return;
        }
       
       alamat = almt.getText();
       notelpon = no.getText();
       email = em.getText();
       prodi = pro.getText();
       //Metode ini mengambil indeks item yang dipilih di dalam ComboBox.
//Jika indeks 1 dipili maka Variabel fakultas diisi dengan "Teknik"//
       if (fkultas.getSelectedIndex() == 1){
            fakultas = "Teknik";
        } else if (fkultas.getSelectedIndex() == 2){
            fakultas = "FEB";
        } else if (fkultas.getSelectedIndex() == 3){
            fakultas = "Hukum";
        }else if (fkultas.getSelectedIndex() == 4){
            fakultas = "Pertanian";
        }else if (fkultas.getSelectedIndex() == 5){
            fakultas = "FKIS";
        }else if (fkultas.getSelectedIndex() == 6){
            fakultas = "FIP";
        }else if (fkultas.getSelectedIndex() == 7){
            fakultas = "FISIB";
        } else {
           javax.swing.JOptionPane.showMessageDialog(this, "Fakultas harus dipilih!");
        return; 
        }
       
        if (cb.isSelected() == true){
            checkbox = "";
        } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Checkbox harus dicentang!");
        return;
        }
       
       hasilrunning hsl = new hasilrunning(nama, tempat, tanggal,jeniskelamin, alamat, notelpon, email, prodi, fakultas);
       hsl.setVisible(true);
       dispose();
    }//GEN-LAST:event_simpanActionPerformed
