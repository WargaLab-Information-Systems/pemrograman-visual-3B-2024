    String nohp= Fnohp.getText();
    String jenis_tabungan= jenistabungan.getSelectedItem().toString(); 
    
    tampil tampil1 = new tampil();
    
    
    
    if (validasi .isSelected()){
        tampil1.setVisible(true);
            tampil1.TFpendaftaran.setText(Fpendaftaran.getText());
            tampil1.TFnama.setText(Fnama.getText());
            tampil1.TFalamat.setText(Falamat.getText());
            tampil1.TFjeniskelamin.setText(jenis_kelamin);
            tampil1.TFnohp.setText(Fnohp.getText());
            tampil1.TFjenistabungan.setText(jenis_tabungan);
        
        this.dispose(); // Tutup JFrame1
    }else{
       JOptionPane.showMessageDialog(null, "centang terlebih dahulu");
    }
    
    
    
