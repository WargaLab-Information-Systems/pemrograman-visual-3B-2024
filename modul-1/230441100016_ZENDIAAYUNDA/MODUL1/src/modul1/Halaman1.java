    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
    // TODO add your handling code here:
    
    String pilihan = (String) jComboBox1.getSelectedItem(); // Ambil item yang dipilih
    if (jComboBox1.getSelectedIndex() == 0 || pilihan == null || pilihan.equals("--Pilih Jurusan--")) {
    javax.swing.JOptionPane.showMessageDialog(this, 
        "Silakan pilih Ekstrakurikuler.", 
        "Peringatan", 
        javax.swing.JOptionPane.ERROR_MESSAGE);
    return; // Kembali, tetap di tampilan saat ini
    }
    
    // Cek apakah jCheckBox1 tidak dicentang
    if (!jCheckBox1.isSelected()) {
        JOptionPane.showMessageDialog(this, "Centang CheckBox Terlebih Dahulu!", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
    } else {
        
    // Jika dicentang, ambil data dan lanjutkan
    String nama = InputNama.getText(); 
    String telepon = InputTelepon.getText();
    String nim = InputNIM.getText();
    String tanggal_lahir = InputTglLahir.getText();
    String alamat = InputAlamat.getText();
    String email = InputEmail.getText();
    String hobi = InputHobi.getText(); 
    String jenis_kelamin = jRadioButtonCowo.isSelected() ? "laki-laki" :
                           jRadioButtonCewe.isSelected() ? "perempuan" : "";
        if (jenis_kelamin.isEmpty()) {
        // Tampilkan pesan peringatan dan kembali ke Tampilan1
        JOptionPane.showMessageDialog(this, "Jenis kelamin harus dipilih!", "PERINGATAN", JOptionPane.WARNING_MESSAGE);
        return; 
    }
  
    String alasan = InputAlasan.getText();
    
    // Jika semua validasi lolos, lanjutkan ke Tampilan2
    new Halaman2(nama, telepon, nim, tanggal_lahir, jenis_kelamin, alamat, email, hobi, pilihan, alasan).setVisible(true);
    dispose(); // Tutup Tampilan1
}

    }//GEN-LAST:event_jButtonSimpanActionPerformed

