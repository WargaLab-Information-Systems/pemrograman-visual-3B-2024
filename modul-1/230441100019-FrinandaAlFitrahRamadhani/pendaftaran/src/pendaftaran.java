    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        String nama = tfNama.getText();
        String nim = tfNim.getText();
        String ttl = tfTTL.getText();
        String jeniskelamin;
        if (tfJK1.isSelected()){
            jeniskelamin = "Laki - Laki";
        } else {
            jeniskelamin = "Perempuan";
        }
        String alamat = tfAlamat.getText();
        String telp = tfTelp.getText();
        String email = tfEmail.getText();
        String fakultas = (String) tfFakultas.getSelectedItem();
        
        boolean vocal = cbVocal.isSelected();
        boolean gitar = cbGitar.isSelected();
        boolean bass = cbBass.isSelected();
        boolean keyboard = cbKeyboard.isSelected();
        boolean drum = cbDrum.isSelected();
        boolean organisasi = cbOrganisasi.isSelected();
        boolean lainnya = cbLainnya.isSelected();
        
        if(nama.isEmpty() || nim.isEmpty() || ttl.isEmpty() || jeniskelamin.isEmpty() || alamat.isEmpty() || telp.isEmpty() || email.isEmpty() || fakultas.isEmpty()){
            JOptionPane.showMessageDialog(null, "Anda Belum Mengisi Semua Data", "Attention", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!verifikasi.isSelected()) {
            JOptionPane.showMessageDialog(null, "Apakah Semua Data Sudah Benar?", "Attention", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        pendaftaran2 p2 = new pendaftaran2();
        p2.tampilkanData(nama, nim, ttl, jeniskelamin, alamat, telp, email, fakultas, vocal, gitar, bass, keyboard, drum, organisasi, lainnya);
        p2.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_SimpanActionPerformed

