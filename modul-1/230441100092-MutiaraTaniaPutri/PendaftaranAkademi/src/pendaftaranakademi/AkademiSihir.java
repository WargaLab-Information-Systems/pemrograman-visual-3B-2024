    private void BSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSimpanActionPerformed
        // TODO add your handling code here:
        if (textNama.getText().isEmpty() || 
        textUmur.getText().isEmpty() || 
        (!buttonLaki.isSelected() && !buttonPr.isSelected()) || 
        comboSU.getSelectedIndex() == -1 || 
        comboKeahlian.getSelectedIndex() == -1 || 
        (!boxElemen.isSelected() && !boxKegelapan.isSelected() && !boxCahaya.isSelected() && !boxRuang.isSelected() && !boxAlam.isSelected())) {
          
        JOptionPane.showMessageDialog(this, "Data harus terisi", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; 
    }

    // Pengecekan khusus untuk checkbox "boxSiap"
    if (!boxSiap.isSelected()) {
        
        JOptionPane.showMessageDialog(this, "Anda belum siap mendaftar! Centang Siap Daftar untuk melanjutkan.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; 
    }
        
        Pendaftar daftar = new Pendaftar();
        daftar.LNama.setText(textNama.getText()); 
        daftar.LUmur.setText(textUmur.getText());
        
        if (buttonLaki.isSelected()){
        daftar.LjenisKelamin.setText(buttonLaki.getText());
        } else if (buttonPr.isSelected()){
        daftar.LjenisKelamin.setText(buttonPr.getText());
        }
             
        // Ini asumsi bahwa frame dua memiliki JTextArea bernama textAreaFrameDua
        StringBuffer kelompokSihir = new StringBuffer();  
        if (boxElemen.isSelected()) {
            kelompokSihir.append(boxElemen.getText()).append(", ");
        }
        if (boxKegelapan.isSelected()) {
            kelompokSihir.append(boxKegelapan.getText()).append(", ");
        }
        if (boxCahaya.isSelected()) {
            kelompokSihir.append(boxCahaya.getText()).append(", ");
        }
        if (boxRuang.isSelected()) {
            kelompokSihir.append(boxRuang.getText()).append(", ");
        }
        if (boxAlam.isSelected()) {
            kelompokSihir.append(boxAlam.getText()).append(", ");
        }        

        // Jika ada teks, hapus koma dan spasi terakhir
        if (kelompokSihir.length() > 0) {
            kelompokSihir.setLength(kelompokSihir.length() - 2);  
        }

        // Memasukkan teks yang disusun ke frame dua
        daftar.LKelompok.setText(kelompokSihir.toString());

        
    
        
        daftar.LsihirUtama.setText(comboSU.getSelectedItem().toString());
        daftar.LKeahlian.setText(comboKeahlian.getSelectedItem().toString());
       
        

        
        daftar.setVisible(true);
    }//GEN-LAST:event_BSimpanActionPerformed

