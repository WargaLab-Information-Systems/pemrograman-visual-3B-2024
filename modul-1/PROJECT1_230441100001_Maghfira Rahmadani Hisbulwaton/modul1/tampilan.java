    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        String Nama=nama.getText();
        String Napang=napang.getText();
        String TTL=ttl.getText();
        String jk=laki.isSelected()?"laki-laki" :
                perempuan.isSelected()? "perempuan" : "";
        if (jk.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(null, "Jenis kelamin belum dipilih!", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
}
        String Jurusan=jurusan.getSelectedItem().toString();
        if (Jurusan.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(null, "Jurusan belum dipilih!", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
}
        String Hobi=hobi.getText();
        String Alamat=alamat.getText();
        
                if (!cekdata.isSelected()) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Silakan centang checkbox terlebih dahulu.", 
            "Peringatan", 
            javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
        }
        tampilan2 Tampilan2= new tampilan2();//
        Tampilan2.setData(Nama,Napang,TTL,jk,Jurusan,Hobi,Alamat);
        Tampilan2.setVisible(true); //menampilkan objek frame Tampilan yang dibuat 
        this.dispose(); 
    }//GEN-LAST:event_simpanActionPerformed

