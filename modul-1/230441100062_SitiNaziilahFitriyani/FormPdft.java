    private void jsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jsimpanActionPerformed
        // TODO add your handling code here:
        output form2 = new output();
        if(jCbasket.isSelected() || jCbadminton.isSelected() || jCteater.isSelected() || jCtari.isSelected() || jCkarya.isSelected() || jCpmr.isSelected()){
            if(jTNama.getText().equals("") || jCkelas.getSelectedItem().toString().equals("") || jTtanggal.getText().equals("") || jTalamat.getText().equals("") || jTtelp.getText().equals("") || jTmail.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Silahkan lengkapi data!", "eror", JOptionPane.ERROR_MESSAGE);
            }else {
                form2.a = jTNama.getText();
                form2.b = jCkelas.getSelectedItem().toString();

                if(jRlaki.isSelected()){    
                    form2.c = jRlaki.getText();
                }else if(jRperempuan.isSelected()){
                    form2.c = jRperempuan.getText();
                }

                form2.d = jTtanggal.getText();
                form2.e = jTalamat.getText();
                form2.f = jTtelp.getText();
                form2.g = jTmail.getText();

                if(jCbasket.isSelected()){
                    form2.h = form2.h + (" Basketball");
                }
                if(jCbadminton.isSelected()){
                    form2.h = form2.h + (" Badminton");
                }
                if(jCteater.isSelected()){
                    form2.h = form2.h + (" Teater");
                }
                if(jCtari.isSelected()){
                    form2.h = form2.h + (" Seni tari");
                }
                if(jCkarya.isSelected()){
                    form2.h = form2.h + (" Karya ilmiah");
                }
                if(jCpmr.isSelected()){
                    form2.h = form2.h + (" PMR");
                }

                form2.setVisible(true);
                this.setVisible(false);
            }
        }else{
            if(!jCbasket.isSelected() || !jCbadminton.isSelected() || !jCteater.isSelected() || !jCtari.isSelected() || !jCkarya.isSelected() || !jCpmr.isSelected()){
              JOptionPane.showMessageDialog(this,"Silahkan pilih minimal 1 ekstrakulikuler!", "Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_jsimpanActionPerformed

