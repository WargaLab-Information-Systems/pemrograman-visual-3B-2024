    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:    
        String jnama = nama.getText();
        String bulan = "";
        String status = "";

        if (jan.isSelected()){
            bulan += "Januari | ";
            status = "Lunas";
        }
        if (feb.isSelected()){
            bulan += "Februari | ";
            status = "Lunas";
        }
        if (mar.isSelected()){
            bulan += "Maret | ";
            status = "Lunas";
        }
        if (apr.isSelected()){
            bulan += "April | ";
            status = "Lunas";
        }
        if (mei.isSelected()){
            bulan += "Mei | ";
            status = "Lunas";
        }
        if (jun.isSelected()){
            bulan += "Juni | ";
            status = "Lunas";
        }
        if (jul.isSelected()){
            bulan += "Juli | ";
            status = "Lunas";
        }
        if (agu.isSelected()){
            bulan += "Agustus | ";
            status = "Lunas";
        }
        if (sep.isSelected()){
            bulan += "September | ";
            status = "Lunas";
        }
        if (okt.isSelected()){
            bulan += "Oktober | ";
            status = "Lunas";
        }
        if (nov.isSelected()){
            bulan += "November | ";
            status = "Lunas";
        }
        if (des.isSelected()){
            bulan += "Desember | ";
            status = "Lunas";
        }

        if (!bulan.isEmpty()) {
            DefaultTableModel dataModel = (DefaultTableModel) jTable.getModel();
            List list = new ArrayList<>();
            list.add(nama.getText());
            list.add(bulan);
            list.add(status);
            dataModel.addRow(list.toArray());
            JOptionPane.showMessageDialog(this, "Pembayaran Sukses");
        } else {
            JOptionPane.showMessageDialog(this, "Pilih setidaknya satu bulan!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

