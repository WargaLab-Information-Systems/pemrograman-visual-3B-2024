    private void btbayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btbayarActionPerformed
        // TODO add your handling code here:
        String namaSiswa = tfnama.getText(); 
        List<String> bulanDibayar = getSelectedMonths();

    
    if (tfhitung.getText().isEmpty() || tfhitung.getText().equals("Rp 0")) {
        JOptionPane.showMessageDialog(null, "Silakan hitung pembayaran terlebih dahulu!");
        return;
    }

    // Cek nama siswa ada/tdk
    boolean siswaSudahAda = false;
    int rowIndex = -1;

    for (int i = 0; i < modelTabel.getRowCount(); i++) {
        if (modelTabel.getValueAt(i, 0).equals(namaSiswa)) {
            siswaSudahAda = true;
            rowIndex = i;
            break;
        }
    }

    // Cek/tambahkan bulan baru jika belum bayar
    if (siswaSudahAda) {
        String bulanLama = modelTabel.getValueAt(rowIndex, 1).toString();
        List<String> bulanLamaList = new ArrayList<>(List.of(bulanLama.split(", ")));

        // catat bulanB
        StringBuilder bulanBaru = new StringBuilder();
        int totalBaru = 0;
        boolean adaBulanBaru = false;

        for (String bulan : bulanDibayar) {
            if (!bulanLamaList.contains(bulan)) {
                bulanBaru.append(bulan).append(", ");
                totalBaru += sppPerBulan; // Tambahbiaya bulanB
                adaBulanBaru = true;
            } else {
                // Jika bulan bayar
                JOptionPane.showMessageDialog(null, "Siswa " + namaSiswa + " sudah membayar untuk bulan " + bulan + "!");
            }
        }

        // Hapus koma dan spasi terakhir jika ada bulan baru
        if (bulanBaru.length() > 0) {
            bulanBaru.setLength(bulanBaru.length() - 2);
        }

        // bulanB? perbarui tabel
        if (adaBulanBaru) {
            String bulanTotal = bulanLama + (bulanBaru.length() > 0 ? ", " + bulanBaru : "");
            modelTabel.setValueAt(bulanTotal, rowIndex, 1);
            
            // Update total pembayaran
            int totalLama = Integer.parseInt(modelTabel.getValueAt(rowIndex, 2).toString().replace("Rp ", "").replace(".", ""));
            modelTabel.setValueAt("Rp " + (totalLama + totalBaru), rowIndex, 2);
        }
    } else {
        // Tambahkan data (tabel) jika siswa baru
        modelTabel.addRow(new Object[]{
            namaSiswa,
            String.join(", ", bulanDibayar), // Menggabungkan bulan yang dipilih
            tfhitung.getText(),
            "" // Kolom status  kosong
        });
    }

    JOptionPane.showMessageDialog(null, "Pembayaran berhasil untuk siswa " + namaSiswa);

    resetForm();
    checkPaymentStatus(namaSiswa);
}
// Metode untuk mendapatkan bulan yang dipilih
private List<String> getSelectedMonths() {
    List<String> bulanDibayar = new ArrayList<>();
    if (januari.isSelected()) bulanDibayar.add("Januari");
    if (februari.isSelected()) bulanDibayar.add("Februari");
    if (maret.isSelected()) bulanDibayar.add("Maret");
    if (april.isSelected()) bulanDibayar.add("April");
    if (mei.isSelected()) bulanDibayar.add("Mei");
    if (juni.isSelected()) bulanDibayar.add("Juni");
    if (juli.isSelected()) bulanDibayar.add("Juli");
    if (agustus.isSelected()) bulanDibayar.add("Agustus");
    if (september.isSelected()) bulanDibayar.add("September");
    if (oktober.isSelected()) bulanDibayar.add("Oktober");
    if (november.isSelected()) bulanDibayar.add("November");
    if (desember.isSelected()) bulanDibayar.add("Desember");
    return bulanDibayar;
}

