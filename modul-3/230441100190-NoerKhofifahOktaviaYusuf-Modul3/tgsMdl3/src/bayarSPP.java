    private void HitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HitungActionPerformed
        String nama = jTextNama.getText();
        double jumlahSPP = Double.parseDouble(jTextJumlah.getText());
    
       
        java.util.List<String> bulanDibayar = new java.util.ArrayList<>();
   

        String[] bulanArray = {"Januari", "Februari", "Maret", "April", "Mei", "Juni", 
                       "Juli", "Agustus", "September", "Oktober", "November", "Desember"};

        JCheckBox[] checkboxes = {januari, februari, maret, april, mei, juni, juli, agustus, 
                          september, oktober, november, desember};

        for (int i = 0; i < bulanArray.length; i++) {
            if (checkboxes[i].isSelected()) {
                bulanDibayar.add(bulanArray[i]);
          }
        }

        int jumlahBulanBelumDibayar = 12 - bulanDibayar.size();

        double totalPembayaran = jumlahSPP * bulanDibayar.size();
        
        String status;
        if (bulanDibayar.size()== 12) {
            status = "Lunas";
        } else if (bulanDibayar.isEmpty()) {
            status = "Belum Bayar";
        } else {
            status = "Menunggak";
        }

        jTextStatus.setText(status);
        jTextTagihan.setText(String.format("%.2f", totalPembayaran));
    }//GEN-LAST:event_HitungActionPerformed

