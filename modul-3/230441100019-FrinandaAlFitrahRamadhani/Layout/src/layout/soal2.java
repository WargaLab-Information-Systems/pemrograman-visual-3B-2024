    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        try{
            String nama = tfNama.getText();
            if (nama.isEmpty () ||tfBayar.getText().isEmpty() || !cbJanuari.isSelected() && !cbFebruari.isSelected() && !cbMaret.isSelected() && !cbApril.isSelected() && !cbMei.isSelected() && !cbJuni.isSelected() && !cbJuli.isSelected() && !cbAgustus.isSelected() && !cbSeptember.isSelected() && !cbOktober.isSelected() && !cbNovember.isSelected() && !cbDesember.isSelected()){
                JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong!", "Message", JOptionPane.ERROR_MESSAGE);
            } else {
                int bayar = Integer.parseInt(tfBayar.getText());
                String bulanBayar = "";
                int perBulan = 500000;

                if(bayar % 500000 != 0){
                    JOptionPane.showMessageDialog(null, "Jumlah Pembayaran yang Anda Masukkan Harus Kelipatan Rp.500.000", "Message", JOptionPane.INFORMATION_MESSAGE);
                    return;
                }

                int totalBulan = 0;
                if(cbJanuari.isSelected()){
                    bulanBayar += "(Januari) ";
                    totalBulan++;
                } if(cbFebruari.isSelected()){
                    bulanBayar += "(Februari) ";
                    totalBulan++;
                } if(cbMaret.isSelected()){
                    bulanBayar += "(Maret) ";
                    totalBulan++;
                } if(cbApril.isSelected()){
                    bulanBayar += "(April) ";
                    totalBulan++;
                } if(cbMei.isSelected()){
                    bulanBayar += "(Mei) ";
                    totalBulan++;
                } if(cbJuni.isSelected()){
                    bulanBayar += "(Juni) ";
                    totalBulan++;
                } if(cbJuli.isSelected()){
                    bulanBayar += "(Juli) ";
                    totalBulan++;
                } if(cbAgustus.isSelected()){
                    bulanBayar += "(Agustus) ";
                    totalBulan++;
                } if(cbSeptember.isSelected()){
                    bulanBayar += "(September) ";
                    totalBulan++;
                } if(cbOktober.isSelected()){
                    bulanBayar += "(Oktober) ";
                    totalBulan++;
                } if(cbNovember.isSelected()){
                    bulanBayar += "(November) ";
                    totalBulan++;
                } if(cbDesember.isSelected()){
                    bulanBayar += "(Desember) ";
                    totalBulan++;
                }
                int totalBayar = perBulan * totalBulan;
                if(bayar < totalBayar){
                    JOptionPane.showMessageDialog(null, "Jumlah pembayaran kurang!\n Total yang harus dibayar adalah " + totalBayar, "Attention", JOptionPane.ERROR_MESSAGE);
                    return;
                } else if(bayar > totalBayar){
                    JOptionPane.showMessageDialog(null, "Jumlah pembayaran lebih!\n Total yang harus dibayar adalah " + totalBayar, "Attention", JOptionPane.ERROR_MESSAGE);
                    return;
                } else {
                    JOptionPane.showMessageDialog(null, "Pembayaran Berhasil Dilakukan!\n Data Telah Disimpan", "Message", JOptionPane.INFORMATION_MESSAGE);
                }
                
                
                tbl.ubahTabel(nama,tfBayar.getText(),bulanBayar);
                tbl.setVisible(true);
                reset();
                
                
            }
        }catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Masukkan Jumlah Pembayaran Dengan Benar!", "Attention", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBayarActionPerformed

