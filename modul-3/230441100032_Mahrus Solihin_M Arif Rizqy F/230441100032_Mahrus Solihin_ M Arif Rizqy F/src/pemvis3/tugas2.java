    private void bayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bayarActionPerformed

        if (nama.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan nama terlebih dahulu!");
            return;
        }
        
        if (nisn.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan nisn terlebih dahulu!");
            return;
        }
        
        if (cash.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan jumlah uang terlebih dahulu!");
            return;
        }

        int total = Integer.parseInt(cetaktotal.getText());
        int uangsiswa = Integer.parseInt(cash.getText());
        
        // Cek apakah uang siswa cukup
        if (uangsiswa < total) {
            JOptionPane.showMessageDialog(this, "Uang tidak cukup untuk melakukan pembayaran!");
        } else {
        
        int uangkembalian = uangsiswa - total;
        this.kembalian.setText(String.valueOf(uangkembalian));

        // Cek apakah data dengan NISN yang sama sudah ada di tabel
        boolean dataDitemukan = false;
        
        for (int i = 0; i < tbl.getRowCount(); i++) {
            if (tbl.getValueAt(i, 1).equals(nisn.getText())) {
                tbl.setValueAt(nama.getText(), i, 0);
                tbl.setValueAt(cetaktotal.getText(), i, 2);
                tbl.setValueAt(cash.getText(), i, 3);
                tbl.setValueAt(kembalian.getText(), i, 4);
                dataDitemukan = true;
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");
                break;
            }
        }

        // Jika data tidak ditemukan, tambahkan baris baru
        if (!dataDitemukan) {
            tbl.addRow(new Object[]{
                nama.getText(), nisn.getText(), cetaktotal.getText(), cash.getText(), kembalian.getText()
            });
            JOptionPane.showMessageDialog(this, "SPP anda sudah dibayar!");
        }

        tbldata.setModel(tbl);

        // Reset semua input setelah pembayaran
        nama.setText("");
        nisn.setText("");
        cetaktotal.setText("");
        cash.setText("");
        kembalian.setText("");
    }
    }//GEN-LAST:event_bayarActionPerformed

