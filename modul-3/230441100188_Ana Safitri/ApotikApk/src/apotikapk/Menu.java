    int total = 0; 
    private void BayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BayarActionPerformed
        if (buttonGroup1.getSelection() == null) {
        JOptionPane.showMessageDialog(null, "Pilih Menu Terlebih Dahulu.");
        return;
    }

    if (Albotil.isSelected()) {
        total = 15000;
    } else if (Aspirin.isSelected()) {
        total = 12000;
    } else if (Bisolvon.isSelected()) {
        total = 10000;
    } else if (Bodrex.isSelected()) {
        total = 7000;
    } else if (Degirol.isSelected()) {
        total = 20000;
    } else if (Dermamin.isSelected()) {
        total = 10000;
    } else if (Dicom.isSelected()) {
        total = 15000;
    } else if (Elsiron.isSelected()) {
        total = 10000;
    } else if (Panadol.isSelected()) {
        total = 5000;
    } else if (Promag.isSelected()) {
        total = 5000;
    }

    int jumlahBayar = 0;
    try {
        jumlahBayar = Integer.parseInt(Pembayaran.getText());
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(null, "Jumlah Yang Anda Masukkan Salah");
        return;
    }

    if (jumlahBayar >= total) {
        int kembalian = jumlahBayar - total;
        JOptionPane.showMessageDialog(null, "Pembayaran Berhasil.");
        Kembalian.setText(String.valueOf(kembalian)); 
    } else {
        JOptionPane.showMessageDialog(null, "Pembayaran Kurang.");
        Kembalian.setText("");
    }
    }//GEN-LAST:event_BayarActionPerformed

