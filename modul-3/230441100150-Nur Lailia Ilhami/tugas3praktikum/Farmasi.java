    private void btnInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInfoActionPerformed
       // Menghapus isi panel detail terlebih dahulu
        DetailObat.removeAll();
        
        // Mengecek checkbox yang dipilih dan menambahkan informasi yang sesuai
        if (CBAciclovir.isSelected()) {
            addDetail("Aciclovir", "Digunakan untuk mengurangi rasa gatal akibat bakteri.", "1-2 kali oles setiap 4-6 jam.");
        }
        if (CBAmpisilin.isSelected()) {
            addDetail("Ampisilin", "Digunakan untuk meredakan infeksi bakteri.", "500 mg, diulang setiap 6-8 jam jika diperlukan.");
        }
        if (CBIbuProfen.isSelected()) {
            addDetail("IbuProfen", "Digunakan untuk mengobati peradangan termasuk nyeri.", "Sesuai petunjuk dokter.");
        }
        if (CBCataflam.isSelected()) {
            addDetail("Cataflam", "Digunakan untuk mengobati sakit gigi.", "20 mg sekali sehari.");
        }
        if (CBDecolsin.isSelected()) {
            addDetail("Decolsin", "Digunakan untuk gejala flu.", "200-400 mg setiap 4-6 jam.");
        }
        if (CBBodrek.isSelected()) {
            addDetail("Bodrek", "Antibiotik untuk mengobati sakit kepala.", "250-500 mg setiap 8 jam.");
        }
        if (CBInzana.isSelected()) {
            addDetail("Inzana", "Digunakan untuk panas dan demam bagi anak.", "Sesuai petunjuk dokter.");
        }
        if (CBParacetamol.isSelected()) {
            addDetail("Paracetamol", "Antijamur untuk antibiotik.", "Sesuai petunjuk dokter.");
        }
        if (CBSangobion.isSelected()) {
            addDetail("Sangobion", "Digunakan untuk menambah darah.", "10-80 mg sekali sehari.");
        }
        if (CBHufagrib.isSelected()) {
            addDetail("Hufagrib", "Digunakan untuk menurunkan demam.", "Dosis ditentukan oleh dokter.");
        }

        // Refresh panel agar perubahan terlihat
        DetailObat.revalidate();
        DetailObat.repaint();
    }

    private void addDetail(String namaObat, String detail, String caraPakai) {
        // Menggunakan JTextField untuk nama obat
        JTextField tfNamaObat = new JTextField(namaObat);
        tfNamaObat.setEditable(false); // Supaya nama obat tidak bisa diubah
        tfNamaObat.setColumns(20); // Mengatur lebar field

        // Menggunakan JTextArea untuk detail obat dan cara pakai
        JTextArea taDetail = new JTextArea(5, 20); // 5 baris, 20 karakter per baris
        taDetail.setText("Detail Obat :" + detail +"\n Cara Pakai" + caraPakai);
        taDetail.setWrapStyleWord(true);
        taDetail.setLineWrap(true);
        taDetail.setEditable(false);
        taDetail.setOpaque(false); // Supaya tampil seperti JLabel
        
        // Menambahkan komponen ke panel detail
        DetailObat.add(new JLabel("Nama Obat:")); // Label untuk penjelasan
        DetailObat.add(tfNamaObat); // JTextField untuk nama obat
        DetailObat.add(taDetail); // JTextArea untuk detail dan cara pakai
    }//GEN-LAST:event_btnInfoActionPerformed

