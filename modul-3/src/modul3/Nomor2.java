    private void btnbayar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbayar1ActionPerformed
        String nama, nim;
        
        nama = innama.getText();
        nim = innim.getText();
        
        String jumlahbulan = (String) jmlbulan.getSelectedItem();
        
        int intjumlahbulan = Integer.parseInt(jumlahbulan);
        
        outnama.setText(nama + " " + nim);
        
        DefaultTableModel model = (DefaultTableModel) tblout.getModel();
        
        for (int hitungan = 1; hitungan <= 6; hitungan++) {
            if(hitungan <= intjumlahbulan){
                model.addRow(new Object[]{hitungan, "Lunas"});
            }else{
                model.addRow(new Object[]{hitungan, "Belum Lunas"});
            }
        }
        
        JOptionPane.showMessageDialog(null, "Anda akan Memasuki tabel Pembayaran", "transaksi Berhasil", JOptionPane.INFORMATION_MESSAGE);
        
        panelform.setVisible(false);
        paneltabel.setVisible(true);
    }//GEN-LAST:event_btnbayar1ActionPerformed

    private void btnconfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconfirmActionPerformed
        int total = 0;
        String jumlahbulan = (String) jmlbulan.getSelectedItem();
        
        int intjumlahbulan = Integer.parseInt(jumlahbulan);
        
        total = total + (intjumlahbulan * 150000);
        
        String strtotal = Integer.toString(total);
        
        outtotal.setText(strtotal);
        
        
    }//GEN-LAST:event_btnconfirmActionPerformed

