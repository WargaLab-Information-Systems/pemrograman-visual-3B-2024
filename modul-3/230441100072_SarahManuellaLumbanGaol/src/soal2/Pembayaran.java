    private void bayarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bayarButtonActionPerformed
        // TODO add your handling code here:
        if (nama.getText().isEmpty() || kelas.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Isi Data Diri Lebih Dulu", "Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String Nama = nama.getText();
        String Kelas = (String) kelas.getSelectedItem();
        ArrayList<String> bulanselesai = new ArrayList<>();
        ArrayList<String> bulanbelum = new ArrayList<>();
        int sisaPembayaran = 0;
        int hargaPerBulan = 650000;
        if (bulan1.isSelected()) {
            bulanselesai.add("Januari");
        } else {
            bulanbelum.add("Januari");
        }
        sisaPembayaran += hargaPerBulan;
        if (bulan2.isSelected()) {
            bulanselesai.add("Februari");
        } else {
            bulanbelum.add("Februri");
        }
        sisaPembayaran += hargaPerBulan;
        if (bulan3.isSelected()) {
            bulanselesai.add("Maret");
        } else {
            bulanbelum.add("Maret");
        }
        sisaPembayaran += hargaPerBulan;
        if (bulan4.isSelected()) {
            bulanselesai.add("April");
        } else {
            bulanbelum.add("April");
        }
        sisaPembayaran += hargaPerBulan;
        if (bulan5.isSelected()) {
            bulanselesai.add("Mei");
        } else {
            bulanbelum.add("Mei");
        }
        sisaPembayaran += hargaPerBulan;
        if (bulan6.isSelected()) {
            bulanselesai.add("Juni");
        } else {
            bulanbelum.add("Juni");
        }
        if (bulan7.isSelected()) {
            bulanselesai.add("Juli");
        } else {
            bulanbelum.add("Juli");
        }
        sisaPembayaran += hargaPerBulan;
        if (bulan8.isSelected()) {
            bulanselesai.add("Agustus");
        } else {
            bulanbelum.add("Agustus");
        }
        sisaPembayaran += hargaPerBulan;
        if (bulan9.isSelected()) {
            bulanselesai.add("September");
        } else {
            bulanbelum.add("September");
        }
        sisaPembayaran += hargaPerBulan;
        if (bulan10.isSelected()) {
            bulanselesai.add("Oktober");
        } else {
            bulanbelum.add("Oktober");
        }
        sisaPembayaran += hargaPerBulan;
        if (bulan11.isSelected()) {
            bulanselesai.add("November");
        } else {
            bulanbelum.add("November");
        }
        sisaPembayaran += hargaPerBulan;
        if (bulan12.isSelected()) {
            bulanselesai.add("Desember");
        } else {
            bulanbelum.add("Desember");
        }
        sisaPembayaran += hargaPerBulan;
        if (bulanselesai.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Isi Bulan Yang Harus Dibayar!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
//
        riwayat.append("""
                        \n====INFORMASI PEMBAYARAN===
                        Nama : """+ Nama+"\n"+"Pembayaran bulan "+ bulanselesai + " berhasil !!!"+"\n");

//        riwayat.append("/n ===Informasi Bayar====" + "Nama : " + Nama + "\nPembayaran Bulan " + bulanselesai + "berhasil");

        if (bulanbelum.isEmpty()) {
            status.append("""
                           \n===STATUS PEMBAYARAN===
                            Nama :  """ + Nama + "\n Kelas  : " + Kelas + "\n Status Pembayaran : Lunas" + "\n");
        } else {
            status.append("""
                           \n===STATUS PEMBAYARAN===
                            Nama :  """ + Nama + "\n Kelas  : " + Kelas + "\n Status Pembayaran : Belum Lunas" + "\n Bulan Yang Belum Dibayar : " + bulanbelum + "\n Bulan Yang Sudah Dibayar : "+ bulanselesai+"\n Sisa Yang Harus Dibayar : Rp." + sisaPembayaran + "\n");
        }
    }//GEN-LAST:event_bayarButtonActionPerformed

