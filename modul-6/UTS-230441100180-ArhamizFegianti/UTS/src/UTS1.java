    private void jBMainActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBMainActionPerformed
        // TODO add your handling code here:
        String input = "";
        
        if (jRB.isSelected()){
            input = "Batu";
        } else if (jRG.isSelected()){
            input = "Gunting";
        } else if (jRK.isSelected()){
            input = "Kertas";
        }
        jTUser.setText(input);
        
        
        String sistem = "";
        
        if (input == "Gunting") {
           jTHasil.setText("Kertas");
           JOptionPane.showMessageDialog(this, "Selamat Anda Menang");
        } else if (input == "Kertas"){
           jTHasil.setText("Gunting");
            JOptionPane.showMessageDialog(this, "Selamat Anda Menang");
        } else if (input == "Batu"){
           jTHasil.setText("Krtas");
           JOptionPane.showMessageDialog(this, "Selamat Anda Menang");           
        }
        
    }//GEN-LAST:event_jBMainActionPerformed

