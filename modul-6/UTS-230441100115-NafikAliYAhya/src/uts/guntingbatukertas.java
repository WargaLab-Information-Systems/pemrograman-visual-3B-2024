    private void playActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playActionPerformed
    int playerOneScore = 0;
         int computerScore = 0;

         int userPlay = 0, computerPlay;


         while(playerOneScore <2 && computerScore <2){
                 computerPlay = (int)(Math.random()*3) +1;
                 if(buttonGroup1.equals("batu"))
                       userPlay = batu;

                 else if (buttonGroup1.equals("kertas"))
                         userPlay =kertas;

                 else if (buttonGroup1.equals("gunting"))
                         userPlay=gunting;


                 if (buttonGroup1.equals("batu"))
                         computerPlay = batu;
                 else if (buttonGroup1.equals("kertas"))
                         computerPlay = kertas;
                 else if (buttonGroup1.equals("gunting"))
                         computerPlay = gunting;

                 if (computerPlay ==batu && userPlay==gunting ){
                         JOptionPane.showMessageDialog(this,"anda kalah", "NOTIF", JOptionPane.INFORMATION_MESSAGE);
                         computerScore++;
                 }
                 if (computerPlay ==batu && userPlay==kertas ){
                         JOptionPane.showMessageDialog(this,"anda menang", "NOTIF", JOptionPane.INFORMATION_MESSAGE);
                         playerOneScore++;
                 }
                 if (computerPlay ==kertas && userPlay==gunting ){
                        JOptionPane.showMessageDialog(this,"anda menang", "NOTIF", JOptionPane.INFORMATION_MESSAGE);
                         playerOneScore++;
                 }
                 if (computerPlay ==kertas && userPlay==batu ){
                        JOptionPane.showMessageDialog(this,"anda kalah", "NOTIF", JOptionPane.INFORMATION_MESSAGE);
                         computerScore++;
                 }
                 if (computerPlay ==gunting && userPlay==batu ){
                         JOptionPane.showMessageDialog(this,"anda menang", "NOTIF", JOptionPane.INFORMATION_MESSAGE);;
                         playerOneScore++;
                 }
                 if (computerPlay ==gunting && userPlay==kertas ){
                         JOptionPane.showMessageDialog(this,"anda kalah", "NOTIF", JOptionPane.INFORMATION_MESSAGE);
                         computerScore++;
                 }
                 if (computerPlay == userPlay ){
                   JOptionPane.showMessageDialog(this,"seri", "NOTIF", JOptionPane.INFORMATION_MESSAGE);

                 }


         }
         if(computerScore > playerOneScore)
                 System.out.println("Computer win score is: - "+ computerScore + " -" + playerOneScore  );
         else
                 System.out.println("Your score is: " + playerOneScore + "-" + computerScore );


  }

