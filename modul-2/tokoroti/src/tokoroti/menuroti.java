    private void pesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesanActionPerformed
    
    if (menubakery.getSelection() == null) { 
    JOptionPane.showMessageDialog(this, "Pilih salah satu menu terlebih dahulu!");
    return;
    } else if (menutopping.getSelection() == null){
        JOptionPane.showMessageDialog(this, "Pilih salah satu toping terlebih dahulu!");
        return;
    } 
    
    if (notopping.isSelected()){
        hargatoping = 0;
    }
    if (hitung > 100000) {
    double diskon = hitung * 0.5;
    hitung -= diskon;
    JOptionPane.showMessageDialog(this, "Anda mendapatkan diskon 5% dengan total : "+hitung, "Diskon", JOptionPane.INFORMATION_MESSAGE);
    }
    hitung = counter * (hargaroti + hargatoping);

    kolomtotal.setText(String.valueOf(hitung));
    if (kolomcash.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan jumlah uang yang dibayarkan", "Error", JOptionPane.INFORMATION_MESSAGE);
        } else if (!kolomcash.getText().matches("\\d+")) {
        JOptionPane.showMessageDialog(this, "Masukkan jumlah uang yang valid (hanya angka)", "Error", JOptionPane.INFORMATION_MESSAGE);
        } else {
        int uangDibayarkan = Integer.parseInt(kolomcash.getText());
        int cembali = uangDibayarkan - hitung;
        if (cembali < 0) {
        JOptionPane.showMessageDialog(this, "Uang yang dibayarkan kurang", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
        kolomkembalian.setText(String.valueOf(cembali));
        return;
        }
}
    }//GEN-LAST:event_pesanActionPerformed

