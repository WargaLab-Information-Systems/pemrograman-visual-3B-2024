    private void jbtotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtotalActionPerformed
        // TODO add your handling code here:
        int harga = 0;

        //        total jumlah pembelian
        if (jbrioche.isSelected() || jcroiss.isSelected() || jbaguet.isSelected() || jcruff.isSelected() || jcrom.isSelected() || jbagel.isSelected()
                || jmuff.isSelected() || jcook.isSelected()) {

            if (jbrioche.isSelected()) {
                harga += 25000 * Integer.parseInt(jjum.getText());
            } else if (jcroiss.isSelected()) {
                harga += 15000 * Integer.parseInt(jjum.getText());
            } else if (jbaguet.isSelected()) {
                harga += 13000 * Integer.parseInt(jjum.getText());
            } else if (jcruff.isSelected()) {
                harga += 28000 * Integer.parseInt(jjum.getText());
            } else if (jcrom.isSelected()) {
                harga += 25000 * Integer.parseInt(jjum.getText());
            } else if (jbagel.isSelected()) {
                harga += 15000 * Integer.parseInt(jjum.getText());
            } else if (jmuff.isSelected()) {
                harga += 20000 * Integer.parseInt(jjum.getText());
            } else if (jcook.isSelected()) {
                harga += 10000 * Integer.parseInt(jjum.getText());
            }

            if (jno.isSelected()) {
                harga += 6000 + Integer.parseInt(jjum.getText());
            } else if (jcoco.isSelected()) {
                harga += 5000 * Integer.parseInt(jjum.getText());
            } else if (jstraw.isSelected()) {
                harga += 4000 * Integer.parseInt(jjum.getText());
            } else if (jmacha.isSelected()) {
                harga += 5000 * Integer.parseInt(jjum.getText());
            }

            if (harga > 100000) {
                double diskon = harga * 0.05;
                harga -= diskon;
            }

            jtotal.setText("Rp. " + harga);

            if (!jcash.getText().isEmpty()) {
                int bayar = Integer.parseInt(jcash.getText());
                int kembali = bayar - harga;
                if (kembali >= 0) {
                    jkembali.setText("Rp. " + kembali);
                } else {
                    jkembali.setText("");
                }
            }

        } else if (!jbrioche.isSelected() || !jcroiss.isSelected() || !jbaguet.isSelected() || !jcruff.isSelected() || !jcrom.isSelected() || !jbagel.isSelected()
                || !jmuff.isSelected() || !jcook.isSelected()) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih menu/topping terlebih dahulu !", "bakery menu", JOptionPane.INFORMATION_MESSAGE);
        }


    }//GEN-LAST:event_jbtotalActionPerformed

