    void hasilKembali(){
        try{
            String hasil = tfCash.getText();
            if(hasil.isEmpty()){
                tfKembali.setText("0");
            } else {
                int cash = Integer.parseInt(hasil);
                int total = Integer.parseInt(tfTotal.getText());
                
                if((cash-total)<0){
                    tfKembali.setText("0");
                }else {
                    tfKembali.setText(Integer.toString(cash-total));
                }
            } 
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Hitung Total dan Masukkan Data Dengan Benar!", "Peringatan", JOptionPane.ERROR_MESSAGE);
        }
    }
    
