    private void minActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_minActionPerformed
         try {
            int jumlahPesanan = Integer.parseInt(pesanan.getText());
            if (jumlahPesanan > 1) {
                jumlahPesanan--;
                pesanan.setText(String.valueOf(jumlahPesanan));
                calculateTotal(null); // Update total harga
            }
        } catch (NumberFormatException e) {
            pesanan.setText("1"); // Jika input tidak valid, set ke 1
        }
    }//GEN-LAST:event_minActionPerformed

