    private void calculateTotal(ItemEvent evt) {
        int hargaRoti = 0;
        int hargaTopping = 0;

        if (gambang.isSelected()) hargaRoti = 15000;
        else if (buaya.isSelected()) hargaRoti = 25000;
        else if (maryam.isSelected()) hargaRoti = 12000;
        else if (chanay.isSelected()) hargaRoti = 16000;
        else if (tiwul.isSelected()) hargaRoti = 11000;
        else if (sobek.isSelected()) hargaRoti = 17000;
        else if (bantal.isSelected()) hargaRoti = 10000;
        else if (sisir.isSelected()) hargaRoti = 18000;

        if (coklat.isSelected()) hargaTopping = 5000;
        else if (cheese.isSelected()) hargaTopping = 4000;
        else if (oreo.isSelected()) hargaTopping = 6000;
        else if (redvelvet.isSelected()) hargaTopping = 7000;

        int jumlahPesanan = 1;
        try {
            jumlahPesanan = Integer.parseInt(pesanan.getText());
        } catch (NumberFormatException e) {
            // Default jumlahPesanan tetap 1 jika tidak menginput apa apa / selain int
        }

        int totalHarga = (hargaRoti + hargaTopping) * jumlahPesanan;

        if (totalHarga > 100000) {
            totalHarga -= totalHarga * 0.05; 
        }

        hasil.setText(String.valueOf(totalHarga));
    } 


