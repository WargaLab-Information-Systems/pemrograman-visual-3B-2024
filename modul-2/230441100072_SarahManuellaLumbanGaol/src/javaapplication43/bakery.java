    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    double totalHarga = Double.parseDouble(hasiltotal.getText().replace("Rp. ", ""));
    double uangDibayarkan = 0;
    try {
        uangDibayarkan = Double.parseDouble(cas.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Masukkan jumlah uang yang valid!", "Note", JOptionPane.ERROR_MESSAGE);
        return;
    }
    double kembalian = uangDibayarkan - totalHarga;
    if (kembalian > 0) {
        balek.setText(String.format("Rp. %.0f", kembalian));
    } else if (kembalian == 0) {
        JOptionPane.showMessageDialog(null, "Uang yang dibayarkan pas!", "Note", JOptionPane.INFORMATION_MESSAGE);
        balek.setText("Rp. 0");
    } else {
        JOptionPane.showMessageDialog(null, "Uang yang dibayarkan kurang!", "Note", JOptionPane.INFORMATION_MESSAGE);
        balek.setText("Rp. 0");
    }

    }//GEN-LAST:event_jButton1ActionPerformed

