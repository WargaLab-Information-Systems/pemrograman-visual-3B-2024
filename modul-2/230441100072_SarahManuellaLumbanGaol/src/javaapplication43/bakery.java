    private void totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalActionPerformed
        // TODO add your handling code here:
    total.addActionListener(new ActionListener() {
            private double jumlah;
    @Override
    public void actionPerformed(ActionEvent e) {
        hitungTotal();
    }
            private void hitungTotal() {
            if (buttonGroup1.getSelection() == null || buttonGroup2.getSelection() == null) {
                    JOptionPane.showMessageDialog( null, "Pilih menu roti dan topping terlebih dahulu!", "Message", JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
                double hargaRoti = 0;
                if(ma.isSelected()) hargaRoti = 13000;
                else if (mb.isSelected()) hargaRoti = 15000;
                else if (mc.isSelected()) hargaRoti = 15000;
                else if (md.isSelected()) hargaRoti = 20000;
                else if (me.isSelected()) hargaRoti = 20000;
                else if (mf.isSelected()) hargaRoti = 15000;
                else if (mg.isSelected()) hargaRoti = 20000;
                else if (mh.isSelected()) hargaRoti = 15000;

                double hargaTopping = 0;
                if (ta.isSelected()) hargaTopping = 5000;
                else if (tb.isSelected()) hargaTopping = 3000;
                else if (tc.isSelected()) hargaTopping = 10000;
                else if (td.isSelected()) hargaTopping = 15000;
                
                int jumlah = Integer.parseInt(txtjumlah.getText());
                double totalHarga = (hargaRoti + hargaTopping) * jumlah;
                if (totalHarga > 100000) {
                    totalHarga -= totalHarga * 0.05;
                }
//                hasiltotal.setText(String.format("Rp. %.0f", totalHarga));
                hasiltotal.setText("Rp. "+totalHarga);
                }
        });
    }//GEN-LAST:event_totalActionPerformed

