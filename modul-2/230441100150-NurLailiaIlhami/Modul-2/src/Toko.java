    private void jbtotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtotalActionPerformed
      if(!RBgandum.isSelected() && !RBdesert.isSelected() && !RBbrownis.isSelected() && !RBrbakar.isSelected()
           && !RBrpisang.isSelected() && !RBpizza.isSelected() && !RBgulung.isSelected() && !RBsus.isSelected()){
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Menu Terlebih Dahulu");
        }else{
            total = 0;
            int jumlah = Integer.parseInt(tfangka.getText()); 
        
            if (RBgandum.isSelected()) total+= 20000;
                else if (RBdesert.isSelected()) total  += 40000;
                else if (RBbrownis.isSelected()) total += 45000;
                else if (RBrbakar.isSelected()) total += 40000;
                else if (RBrpisang.isSelected()) total += 25000;
                else if (RBpizza.isSelected()) total += 10000;
                else if (RBgulung.isSelected()) total += 20000;
                else if (RBsus.isSelected()) total += 15000;
            
             if (RBmisis.isSelected()) total+= 2000;
                else if (RBKacang.isSelected()) total += 4000;
                else if (RBmisis.isSelected()) total += 2000;
                else if (RBBoba.isSelected()) total += 3000;
                else if (RBKeju.isSelected()) total += 3000;
             
        }
            //diskon
            int jumlah = Integer.parseInt(tfangka.getText()); // Ambil nilai jumlah
            total *= jumlah;
             if (total > 100000) {
                    total *= 0.95;
                }
            tftotal.setText(String.valueOf(total));
            try {
        // Ambil total harga dari TextField tfTotal
        int total = Integer.parseInt(tftotal.getText());
    
        // Ambil jumlah uang yang diinputkan oleh pembeli dari TextField tfCash
        int uangPembeli = Integer.parseInt(tfcash.getText());

        // Hitung kembalian
        int kembalian = uangPembeli - total;

        // Tampilkan kembalian di TextField tfKembalian
        if (kembalian < 0) {
            tfkembali.setText("Uang kurang");
        } else {
            tfkembali.setText(String.valueOf(kembalian));
        }
    } catch (NumberFormatException e) {
        // Jika input belum valid (misalnya kosong atau bukan angka), kosongkan tfKembalian
        tfkembali.setText("");
    }
        

       
    }//GEN-LAST:event_jbtotalActionPerformed

