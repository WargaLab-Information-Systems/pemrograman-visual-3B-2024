    private void TotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TotalActionPerformed
        // TODO add your handling code here:
        int hargaMenu = 0;
        int hargaTopping = 0;
     
        // Cek menu roti yang dipilih dan setel harganya
        if (Roti1.isSelected()) { 
            hargaMenu = 15000; 
        } else if (Roti2.isSelected()) {
            hargaMenu = 10000;
        } else if (Roti3.isSelected()) {
            hargaMenu = 20000;
        } else if (Roti4.isSelected()) {
            hargaMenu = 10000;
        } else if (Roti5.isSelected()) {
            hargaMenu = 10000;
        } else if (Roti6.isSelected()) {
            hargaMenu = 5000;
        } else if (Roti7.isSelected()) {
            hargaMenu = 5000;
        } else if (Roti8.isSelected()) {
            hargaMenu = 15000;
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Menu Roti Yang Di Inginkan!", "PERINGATAN", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        // Cek topping yang dipilih dan harganya
        if (Topping1.isSelected()) {
            hargaTopping = 3000;
        } else if (Topping2.isSelected()) {
            hargaTopping = 3000;
        } else if (Topping3.isSelected()) {
            hargaTopping = 5000;
        } else if (Topping4.isSelected()) {
            hargaTopping = 5000;
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Topping Yang Di Inginkan!", "PERINGATAN", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
                
        int totalharga = (hargaMenu + hargaTopping) * jumlahPembelian;

        // Diskon jika total lebih dari Rp. 100.000
        if (totalharga > 100000) {
            totalharga -= totalharga * 5 / 100;
        } TotalPesanan.setText("Rp. " + totalharga);
                
        String PembayaranPembeli = Pembayaran.getText();
            if (PembayaranPembeli.isEmpty()) { // Periksa apakah teks kosong
                Kembalian.setText("");
            return; 
            }
                
        int UangPembayaran = Integer.parseInt(PembayaranPembeli); // Konversi ke integer
        int kembalian = UangPembayaran - totalharga; 
        // Jika uang cash tidak cukup
            if (kembalian < 0) { 
                Kembalian.setText("");
                JOptionPane.showMessageDialog(this, "Uang Anda tidak cukup!", "PERINGATAN", JOptionPane.INFORMATION_MESSAGE);
            } else {
                Kembalian.setText("Rp. " + kembalian);
            }
    }//GEN-LAST:event_TotalActionPerformed

