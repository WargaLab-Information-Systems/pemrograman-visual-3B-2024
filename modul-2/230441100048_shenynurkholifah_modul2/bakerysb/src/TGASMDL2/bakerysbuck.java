    private void btntotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntotalActionPerformed
       if (!chese.isSelected() && !bites.isSelected() && !bagel.isSelected() && !choco.isSelected()
        && !almon.isSelected() && !srikaya.isSelected() && !lapislegit.isSelected() && !lapismoza.isSelected()) {
   //kondisi ini memeriksa apakah semua checkbox yang terkait dengan item tidak ada yang dipilih.
        JOptionPane.showMessageDialog(null, "Silahkan Pilih Menu Terlebih Dahulu");
    
       } else {
        int total = 0;
        int jumlahpesanan = Integer.parseInt(jumlah.getText()); // Ambil nilai jumlah
        
        // Variabel untuk menampung menu yang dipilih
        String selectedMenu = "";

        // Memeriksa menu mana yang dipilih, disimpan ke dalam variabel selectedMenu.

        if (chese.isSelected()) selectedMenu = "chese";
        if (bites.isSelected()) selectedMenu = "bites";
        if (bagel.isSelected()) selectedMenu = "bagel";
        if (choco.isSelected()) selectedMenu = "choco";
        if (almon.isSelected()) selectedMenu = "almon";
        if (srikaya.isSelected()) selectedMenu = "srikaya";
        if (lapislegit.isSelected()) selectedMenu = "lapislegit";
        if (lapismoza.isSelected()) selectedMenu = "lapismoza";

        // Gunakan switch-case untuk menentukan harga berdasarkan menu yang dipilih
        switch (selectedMenu) {
            case "chese":
                total += 200000;
                break;
            case "bites":
                total += 90000;
                break;
            case "bagel":
                total += 150000;
                break;
            case "choco":
                total += 150000;
                break;
            case "almon":
                total += 225000;
                break;
            case "srikaya":
                total += 320000;
                break;
            case "lapislegit":
                total += 180000;
                break;
            case "lapismoza":
                total += 100000;
                break;
            default:
                JOptionPane.showMessageDialog(null, "Silahkan pilih menu terlebih dahulu");
                return;
        }

        // Tambahan harga untuk topping
        if (oreo.isSelected()) total += 2000;
        if (keju.isSelected()) total += 4000;
        if (matcha.isSelected()) total += 2000;

        // Mengalikan total dengan jumlah pesanan
        total *= jumlahpesanan;

        // Berlakukan diskon 5% jika total lebih dari 100000
        if (total > 100000) {
            total *= 0.95;
            JOptionPane.showMessageDialog(this, "Selamat Anda dapat diskon 5%!");
        }

        // Tampilkan total di label atau textfield
        this.total.setText(String.valueOf(total));

        // Hitung kembalian jika ada input uang pembeli
        try {
            int dolarpembelinya = Integer.parseInt(cash.getText());
            int kembalian = dolarpembelinya - total;

            // Cek apakah kembalian negatif (uang kurang)
            if (kembalian < 0) {
                kembali.setText("Uang tidak cukup");
            } else {
                kembali.setText(String.valueOf(kembalian));
            }
        } catch (NumberFormatException ex) {
            kembali.setText("Error");  // Jika input uang bukan angka
        }
      }
            
    }//GEN-LAST:event_btntotalActionPerformed

