    private void totalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalActionPerformed
        total.addActionListener(new ActionListener() {
            private double jumlah;
            @Override
    public void actionPerformed(ActionEvent e) {
        hitungTotal();
    }
            private void hitungTotal() {
            if (buttonGroup1.getSelection() == null || buttonGroup2.getSelection() == null) {
                    JOptionPane.showMessageDialog( null, "Pilih menu roti dan topping terlebih dahulu!", "Message", JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
                double hargaRoti = 0;
                if(a.isSelected()) hargaRoti = 7000;
                else if (b.isSelected()) hargaRoti = 5000;
                else if (c.isSelected()) hargaRoti = 6000;
                else if (d.isSelected()) hargaRoti = 5000;
                else if (e.isSelected()) hargaRoti = 12000;
                else if (f.isSelected()) hargaRoti = 12000;
                else if (g.isSelected()) hargaRoti = 7000;
                else if (h.isSelected()) hargaRoti = 5000;

                double hargaTopping = 0;
                if (i.isSelected()) hargaTopping = 3000;
                else if (j.isSelected()) hargaTopping = 3000;
                else if (k.isSelected()) hargaTopping = 4000;
                else if (l.isSelected()) hargaTopping = 3000;
                int jumlah = Integer.parseInt(jumlahbrg.getText());
                double totalHarga = (hargaRoti + hargaTopping) * jumlah;
                if (totalHarga > 100000) {
                    totalHarga -= totalHarga * 0.05;
                }
                harto.setText(String.format("Rp. %.0f", totalHarga));
                }
        });
                                                               
    }//GEN-LAST:event_totalActionPerformed

