    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
    double totalHarga = 0;
    try {
        totalHarga = Double.parseDouble(harto.getText().replace("Rp. ", ""));
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, " Uang yang anda masukkan tidak valid!", "Note", JOptionPane.ERROR_MESSAGE);
    }
    double uangDibayarkan = 0;
    try {
        uangDibayarkan = Double.parseDouble(cash.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Masukkan jumlah uang yang valid!", "Note", JOptionPane.ERROR_MESSAGE);
        return;
    }
    double kembalian = uangDibayarkan - totalHarga;
    if (kembalian > 0) {
        sisa.setText(String.format("Rp. %.0f", kembalian));
    }else if (kembalian==0){
        JOptionPane.showMessageDialog(null, "Uang yang dibayarkan pas!", "Note", JOptionPane.INFORMATION_MESSAGE);
        sisa.setText("Rp. 0");
    }else {
        JOptionPane.showMessageDialog(null, "Uang yang dibayarkan kurang!", "Note", JOptionPane.INFORMATION_MESSAGE);
        sisa.setText("Rp. 0");
    }    
    }//GEN-LAST:event_hitungActionPerformed

