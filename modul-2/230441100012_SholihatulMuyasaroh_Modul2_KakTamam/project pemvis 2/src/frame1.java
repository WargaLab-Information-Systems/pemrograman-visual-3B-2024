    private void tfcashActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfcashActionPerformed
        // TODO add your handling code here:
        if (!tfcash.getText().isEmpty() && !tftotal.getText().isEmpty()) {
        try {
            int cash = Integer.parseInt(tfcash.getText()); 
            int total = Integer.parseInt(tftotal.getText());  
           
            if (cash < total) {
                JOptionPane.showMessageDialog(this, "Uang cash tidak cukup.", "Error", JOptionPane.ERROR_MESSAGE);
                tfkembali.setText(""); 
                return;
            }
            
            int kembali = cash - total;  
            tfkembali.setText(String.valueOf(kembali)); 
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan angka yang valid.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Silakan masukkan cash dan tampilkan total.", "Warning", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_tfcashActionPerformed

