    private void bttotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttotalActionPerformed
        // TODO add your handling code here:
        harga_roti = 0;
        harga_topping = 0;
        
        if (rbbakar.isSelected()) harga_roti = harga_Rbakar;
        else if (rbdonat.isSelected()) harga_roti = harga_donat;
        else if (rbsobek.isSelected()) harga_roti = harga_Rsobek;
        else if (rbcrombo.isSelected()) harga_roti = harga_crombo;
        else if (rbbuaya.isSelected()) harga_roti = harga_Rbuaya;
        else if (rbbantal.isSelected()) harga_roti = harga_Rbantal;
        else if (rbwaffle.isSelected()) harga_roti = harga_waffle;
        else if (rbcrois.isSelected()) harga_roti = harga_crois;
        
        if (rbtanpa.isSelected()) harga_topping = harga_Ttopping;
        else if (rbcokelat.isSelected()) harga_topping = harga_cokelat;
        else if (rbkacang.isSelected()) harga_topping = harga_kacang;
        else if (rbstraw.isSelected()) harga_topping = harga_strawberry;
        
        if (!rbbakar.isSelected() && !rbdonat.isSelected() && !rbsobek.isSelected() && !rbcrombo.isSelected() && 
            !rbbuaya.isSelected() && !rbbantal.isSelected() && !rbwaffle.isSelected() && !rbcrois.isSelected()) {
        JOptionPane.showMessageDialog(null,"pilih menu roti terlebih dahulu", "pesan", JOptionPane.INFORMATION_MESSAGE);
        return;
        }
        
        if (!rbtanpa.isSelected() && !rbcokelat.isSelected() && !rbkacang.isSelected() && !rbstraw.isSelected()) {
        JOptionPane.showMessageDialog(null,"pilih topping roti terlebih dahulu", "pesan", JOptionPane.INFORMATION_MESSAGE);
        return;
        }
        
        
        int total_harga = (harga_roti + harga_topping) * jumlah;
        int diskon = 0;
        int sebelum_diskon = total_harga;
        
        if (total_harga > 100000) {
            diskon = total_harga * 5 / 100;
            total_harga = total_harga - diskon;  
            
            JOptionPane.showMessageDialog(this,"Anda mendapatkan diskon sebesar: Rp " + diskon +
                    "\nHarga sebelum diskon: " + sebelum_diskon +"\nTotal setelah diskon: Rp " + 
                    total_harga, "SELAMAT!!!", JOptionPane.INFORMATION_MESSAGE);
        }
        tftotal.setText(String.valueOf(total_harga));
        
        
        
    }//GEN-LAST:event_bttotalActionPerformed

