    private void btnMinusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMinusActionPerformed
        int jumlah = Integer.parseInt(tfJumlah.getText());
        jumlah--;
        tfJumlah.setText(Integer.toString(jumlah));
        
        if(jumlah <= 1){
            btnMinus.setEnabled(false);
            return;
        }
    }//GEN-LAST:event_btnMinusActionPerformed

    private void btnPlusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlusActionPerformed
        int jumlah = Integer.parseInt(tfJumlah.getText());
        jumlah++;
        tfJumlah.setText(Integer.toString(jumlah));
        
        if(jumlah >= 0){
            btnMinus.setEnabled(true);
        }
    }//GEN-LAST:event_btnPlusActionPerformed

    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
        int menu;
        if(RBRoti1.isSelected()){
        menu=5000;
        }else if(RBRoti2.isSelected()){
        menu=5000;
        }else if(RBRoti3.isSelected()){
        menu=9000;
        }else if(RBRoti4.isSelected()){
        menu=8000;
        }else if(RBRoti5.isSelected()){
        menu=10000;
        }else if(RBRoti6.isSelected()){
        menu=8000;
        }else if(RBRoti7.isSelected()){
        menu=7000;
        }else if(RBRoti8.isSelected()){
        menu=10000;
        }else{
            menu=0;
        }
        
        int topping;
        if(RBTopping1.isSelected()){
        topping = 0;
        }else if(RBTopping2.isSelected()){
        topping = 3000;
        }else if(RBTopping3.isSelected()){
        topping = 2000;
        }else if(RBTopping4.isSelected()){
        topping = 5000;
        }else{
            topping=0;
        }
        if(menu == 0){
         JOptionPane.showMessageDialog(null,"Mohon Pilih Menu Terlebih Dahulu", "Pesan", JOptionPane.INFORMATION_MESSAGE);
        }
        
        int total = (menu + topping) * Integer.parseInt(tfJumlah.getText());
        tfTotal.setText(Integer.toString((total > 100000) ? total - (total * 5 / 100) : total));
    }//GEN-LAST:event_btnTotalActionPerformed

