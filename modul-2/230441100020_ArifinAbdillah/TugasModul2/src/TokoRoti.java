    private void hitungKembalian(){
        String text = tfCash.getText();
        try {
            if(text.isEmpty()){
            tfKembalian.setText("0");
           }else{

            int cash=Integer.parseInt(text);
            int total=Integer.parseInt(tfTotal.getText());

            if(cash < total){
              tfKembalian.setText("0");
            }else{
              tfKembalian.setText(Integer.toString(cash-total));
            }
           }
         }catch(NumberFormatException e){
             JOptionPane.showMessageDialog(null,"Inputan Harus Berupa Angka", "Peringatan", JOptionPane.ERROR_MESSAGE);
         }
    }
